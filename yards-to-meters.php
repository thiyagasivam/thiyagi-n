<script>
function updateConversion() {
  const yardsValue = parseFloat(document.getElementById('yardsValue').value);
  const metersResult = document.getElementById('metersResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(yardsValue) || yardsValue < 0) {
    metersResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert yards to meters (1 yard = 0.9144 meters)
  const metersValue = yardsValue * 0.9144;
  
  // Format the result
  let formattedResult;
  if (metersValue >= 1000) {
    formattedResult = metersValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (metersValue >= 1) {
    formattedResult = metersValue.toFixed(3);
  } else {
    formattedResult = metersValue.toFixed(6);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  metersResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const yardsValue = document.getElementById('yardsValue').value;
  const metersResult = document.getElementById('metersResult').value;
  const copyText = `${yardsValue} yards = ${metersResult} meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('yardsValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('yardsValue').focus();
  document.getElementById('yardsValue').value = 100;
  updateConversion();
});
</script>

