<script>
function updateConversion() {
  const gramsValue = parseFloat(document.getElementById('gramsValue').value);
  const poundsResult = document.getElementById('poundsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(gramsValue) || gramsValue < 0) {
    poundsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert grams to pounds (1 g = 0.00220462 lbs)
  const poundsValue = gramsValue * 0.00220462;
  
  // Format the result
  let formattedResult;
  if (poundsValue >= 1000) {
    formattedResult = poundsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (poundsValue >= 1) {
    formattedResult = poundsValue.toFixed(4);
  } else {
    formattedResult = poundsValue.toFixed(6);
  }
  
  poundsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const gramsValue = document.getElementById('gramsValue').value;
  const poundsResult = document.getElementById('poundsResult').value;
  const copyText = `${gramsValue} grams = ${poundsResult} pounds`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('gramsValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('gramsValue').focus();
  document.getElementById('gramsValue').value = 1000;
  updateConversion();
});
</script>

