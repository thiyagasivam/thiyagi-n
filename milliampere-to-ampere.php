<script>
function updateConversion() {
  const milliampereValue = parseFloat(document.getElementById('milliampereValue').value);
  const ampereResult = document.getElementById('ampereResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(milliampereValue) || milliampereValue < 0) {
    ampereResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert milliampere to ampere
  // 1 ampere = 1,000 milliampere
  const ampereValue = milliampereValue / 1000;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (ampereValue >= 1000) {
    formattedResult = ampereValue.toLocaleString('en-US', {maximumFractionDigits: 3});
  } else if (ampereValue >= 100) {
    formattedResult = ampereValue.toFixed(3);
  } else if (ampereValue >= 10) {
    formattedResult = ampereValue.toFixed(4);
  } else if (ampereValue >= 1) {
    formattedResult = ampereValue.toFixed(5);
  } else if (ampereValue >= 0.1) {
    formattedResult = ampereValue.toFixed(4);
  } else if (ampereValue >= 0.01) {
    formattedResult = ampereValue.toFixed(5);
  } else {
    formattedResult = ampereValue.toFixed(6);
  }
  
  ampereResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const milliampereValue = document.getElementById('milliampereValue').value;
  const ampereResult = document.getElementById('ampereResult').value;
  const copyText = `${milliampereValue} milliampere = ${ampereResult} ampere`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('milliampereValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('milliampereValue').focus();
  document.getElementById('milliampereValue').value = 1000;
  updateConversion();
});
