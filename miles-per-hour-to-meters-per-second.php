<script>
function updateConversion() {
  const mphInput = document.getElementById('mphValue');
  const mphValue = parseFloat(mphInput.value);
  const msResult = document.getElementById('msResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(mphValue) || mphValue < 0) {
    msResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    msResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert miles per hour to meters per second (1 mph = 0.44704 m/s)
  const msValue = mphValue * 0.44704;
  
  let formattedResult = msValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  msResult.value = formattedResult;
  msResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMph = mphValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMph} mph = ${formattedResult} m/s`;
  liveConversion.classList.remove('hidden');
  
  mphInput.classList.add('border-blue-500');
}

function copyResult() {
  const mphValue = document.getElementById('mphValue').value;
  const msResult = document.getElementById('msResult').value;
  const copyText = `${mphValue} mph = ${msResult} m/s`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('mphValue').value = '';
  document.getElementById('msResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('msResult').classList.remove('result-highlight');
  document.getElementById('mphValue').classList.remove('border-blue-500');
  document.getElementById('mphValue').focus();
}

document.getElementById('mphValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const mphInput = document.getElementById('mphValue');
  mphInput.focus();
  
  setTimeout(() => {
    mphInput.value = 60;
    updateConversion();
  }, 500);
});
</script>

