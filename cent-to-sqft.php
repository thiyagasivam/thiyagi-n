<script>
function updateConversion() {
  const centValue = parseFloat(document.getElementById('centValue').value);
  const sqftResult = document.getElementById('sqftResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(centValue) || centValue < 0) {
    sqftResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cent to square feet (1 cent = 435.6 square feet)
  const sqftValue = centValue * 435.6;
  
  // Format the result
  let formattedResult;
  if (sqftValue >= 10000) {
    formattedResult = sqftValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqftValue >= 100) {
    formattedResult = sqftValue.toFixed(1);
  } else {
    formattedResult = sqftValue.toFixed(2);
  }
  
  sqftResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const centValue = document.getElementById('centValue').value;
  const sqftResult = document.getElementById('sqftResult').value;
  const copyText = `${centValue} cents = ${sqftResult} square feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('centValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('centValue').focus();
  document.getElementById('centValue').value = 10;
  updateConversion();
});
</script>

