<script>
function updateConversion() {
  const meterValue = parseFloat(document.getElementById('meterValue').value);
  const inchResult = document.getElementById('inchResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(meterValue) || meterValue < 0) {
    inchResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert meters to inches (1 m = 39.3701 in)
  const inchValue = meterValue * 39.3701;
  
  // Format the result
  let formattedResult;
  if (inchValue >= 10000) {
    formattedResult = inchValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (inchValue >= 1000) {
    formattedResult = inchValue.toFixed(2);
  } else if (inchValue >= 100) {
    formattedResult = inchValue.toFixed(3);
  } else if (inchValue >= 10) {
    formattedResult = inchValue.toFixed(4);
  } else {
    formattedResult = inchValue.toFixed(5);
  }
  
  inchResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const meterValue = document.getElementById('meterValue').value;
  const inchResult = document.getElementById('inchResult').value;
  const copyText = `${meterValue} meters = ${inchResult} inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('meterValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('meterValue').focus();
  document.getElementById('meterValue').value = 2;
  updateConversion();
});
</script>

