<script>
let isUpdating = false;

function convertPascalToNewtonSquareMeter(pascal) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(pascal) && pascal !== '') {
        // 1 Pa = 1 N/m² (identical units)
        const newtonSquareMeter = pascal;
        document.getElementById('newtonSquareMeterInput').value = newtonSquareMeter;
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-slate-600 mr-2"></i>
            <strong>${pascal.toLocaleString()} Pa = ${newtonSquareMeter.toLocaleString()} N/m²</strong>
        `;
    } else {
        document.getElementById('newtonSquareMeterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertNewtonSquareMeterToPascal(newtonSquareMeter) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(newtonSquareMeter) && newtonSquareMeter !== '') {
        // 1 N/m² = 1 Pa (identical units)
        const pascal = newtonSquareMeter;
        document.getElementById('pascalInput').value = pascal;
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-slate-600 mr-2"></i>
            <strong>${newtonSquareMeter.toLocaleString()} N/m² = ${pascal.toLocaleString()} Pa</strong>
        `;
    } else {
        document.getElementById('pascalInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('pascalInput').value = '';
    document.getElementById('newtonSquareMeterInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const pascalValue = document.getElementById('pascalInput').value;
    const newtonSquareMeterValue = document.getElementById('newtonSquareMeterInput').value;
    
    document.getElementById('pascalInput').value = newtonSquareMeterValue;
    document.getElementById('newtonSquareMeterInput').value = pascalValue;
    
    if (newtonSquareMeterValue) {
        convertPascalToNewtonSquareMeter(parseFloat(newtonSquareMeterValue));
    } else if (pascalValue) {
        convertNewtonSquareMeterToPascal(parseFloat(pascalValue));
    }
}

// Add event listeners
document.getElementById('pascalInput').addEventListener('input', function() {
    convertPascalToNewtonSquareMeter(parseFloat(this.value));
});

document.getElementById('newtonSquareMeterInput').addEventListener('input', function() {
    convertNewtonSquareMeterToPascal(parseFloat(this.value));
});
</script>

