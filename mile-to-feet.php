<script>
function updateConversion() {
  const mileValue = parseFloat(document.getElementById('mileValue').value);
  const feetResult = document.getElementById('feetResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mileValue) || mileValue < 0) {
    feetResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert miles to feet (1 mile = 5,280 feet)
  const feetValue = mileValue * 5280;
  
  // Format the result
  let formattedResult;
  if (feetValue >= 1000000) {
    formattedResult = feetValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (feetValue >= 10000) {
    formattedResult = feetValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (feetValue >= 1000) {
    formattedResult = feetValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else {
    formattedResult = feetValue.toFixed(2);
  }
  
  feetResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mileValue = document.getElementById('mileValue').value;
  const feetResult = document.getElementById('feetResult').value;
  const copyText = `${mileValue} miles = ${feetResult} feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('mileValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mileValue').focus();
  document.getElementById('mileValue').value = 1;
  updateConversion();
});
</script>

