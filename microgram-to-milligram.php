<script>
function updateConversion() {
  const microgramInput = document.getElementById('microgramValue');
  const microgramValue = parseFloat(microgramInput.value);
  const milligramResult = document.getElementById('milligramResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(microgramValue) || microgramValue < 0) {
    milligramResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    milligramResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert micrograms to milligrams (1 µg = 0.001 mg)
  const milligramValue = microgramValue / 1000;
  
  let formattedResult = milligramValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  milligramResult.value = formattedResult;
  milligramResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMicrogram = microgramValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMicrogram} µg = ${formattedResult} mg`;
  liveConversion.classList.remove('hidden');
  
  microgramInput.classList.add('border-emerald-500');
}

function copyResult() {
  const microgramValue = document.getElementById('microgramValue').value;
  const milligramResult = document.getElementById('milligramResult').value;
  const copyText = `${microgramValue} µg = ${milligramResult} mg`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('microgramValue').value = '';
  document.getElementById('milligramResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('milligramResult').classList.remove('result-highlight');
  document.getElementById('microgramValue').classList.remove('border-emerald-500');
  document.getElementById('microgramValue').focus();
}

document.getElementById('microgramValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const microgramInput = document.getElementById('microgramValue');
  microgramInput.focus();
  
  setTimeout(() => {
    microgramInput.value = 1000;
    updateConversion();
  }, 500);
});
</script>

