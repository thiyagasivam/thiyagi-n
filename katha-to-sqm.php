<script>
function updateConversion() {
  const kathaValue = parseFloat(document.getElementById('kathaValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kathaValue) || kathaValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert katha to square meters (1 katha = 66.89 square meters in West Bengal)
  const sqmValue = kathaValue * 66.89;
  
  // Format the result
  let formattedResult;
  if (sqmValue >= 10000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqmValue >= 100) {
    formattedResult = sqmValue.toFixed(1);
  } else {
    formattedResult = sqmValue.toFixed(2);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kathaValue = document.getElementById('kathaValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${kathaValue} katha = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kathaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kathaValue').focus();
  document.getElementById('kathaValue').value = 5;
  updateConversion();
});
</script>

