// Enhanced conversion function
function updateConversion() {
  const gbInput = document.getElementById('gbValue');
  const gbValue = parseFloat(gbInput.value);
  const mbResult = document.getElementById('mbResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(gbValue) || gbValue < 0) {
    mbResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    mbResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert GB to MB (1 GB = 1000 MB)
  const mbValue = gbValue * 1000;
  
  // Format result
  let formattedResult;
  if (mbValue >= 1000000) {
    formattedResult = mbValue.toExponential(3);
  } else {
    formattedResult = mbValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 2 
    });
  }
  
  // Update result field
  mbResult.value = formattedResult;
  mbResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedGB = gbValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedGB} GB = ${formattedResult} MB`;
  liveConversion.classList.remove('hidden');
  
  gbInput.classList.add('border-purple-500');
}

// Copy function
function copyResult() {
  const gbValue = document.getElementById('gbValue').value;
  const mbResult = document.getElementById('mbResult').value;
  const copyText = `${gbValue} GB = ${mbResult} MB`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('gbValue').value = '';
  document.getElementById('mbResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('mbResult').classList.remove('result-highlight');
  document.getElementById('gbValue').classList.remove('border-purple-500');
  document.getElementById('gbValue').focus();
}

// Event listeners
document.getElementById('gbValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Auto-focus and demo value on page load
window.addEventListener('DOMContentLoaded', () => {
  const gbInput = document.getElementById('gbValue');
  gbInput.focus();
  
  setTimeout(() => {
    gbInput.value = 1;
    updateConversion();
  }, 500);
});
</script>

