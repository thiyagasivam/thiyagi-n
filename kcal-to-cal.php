<script>
function updateConversion() {
  const kcalInput = document.getElementById('kcalValue');
  const kcalValue = parseFloat(kcalInput.value);
  const calResult = document.getElementById('calResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(kcalValue) || kcalValue < 0) {
    calResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    calResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert kilocalories to calories (1 kcal = 1000 cal)
  const calValue = kcalValue * 1000;
  
  let formattedResult = calValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  calResult.value = formattedResult;
  calResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedKcal = kcalValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedKcal} kcal = ${formattedResult} cal`;
  liveConversion.classList.remove('hidden');
  
  kcalInput.classList.add('border-amber-500');
}

function copyResult() {
  const kcalValue = document.getElementById('kcalValue').value;
  const calResult = document.getElementById('calResult').value;
  const copyText = `${kcalValue} kcal = ${calResult} cal`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('kcalValue').value = '';
  document.getElementById('calResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('calResult').classList.remove('result-highlight');
  document.getElementById('kcalValue').classList.remove('border-amber-500');
  document.getElementById('kcalValue').focus();
}

document.getElementById('kcalValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const kcalInput = document.getElementById('kcalValue');
  kcalInput.focus();
  
  setTimeout(() => {
    kcalInput.value = 2.5;
    updateConversion();
  }, 500);
});
</script>

