function updateConversion() {
  const byteValue = parseFloat(document.getElementById('byteValue').value);
  const kilobyteResult = document.getElementById('kilobyteResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(byteValue) || byteValue < 0) {
    kilobyteResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert bytes to kilobytes
  // 1 kilobyte = 1,024 bytes
  const kilobyteValue = byteValue / 1024;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (kilobyteValue >= 1000) {
    formattedResult = kilobyteValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (kilobyteValue >= 100) {
    formattedResult = kilobyteValue.toFixed(2);
  } else if (kilobyteValue >= 10) {
    formattedResult = kilobyteValue.toFixed(3);
  } else if (kilobyteValue >= 1) {
    formattedResult = kilobyteValue.toFixed(4);
  } else {
    formattedResult = kilobyteValue.toFixed(6);
  }
  
  kilobyteResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const byteValue = document.getElementById('byteValue').value;
  const kilobyteResult = document.getElementById('kilobyteResult').value;
  const copyText = `${byteValue} bytes = ${kilobyteResult} kilobytes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('byteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('byteValue').focus();
  document.getElementById('byteValue').value = 1024;
  updateConversion();
});
</script>

