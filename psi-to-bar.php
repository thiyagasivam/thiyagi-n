<script>
function updateConversion() {
  const psiInput = document.getElementById('psiValue');
  const psiValue = parseFloat(psiInput.value);
  const barResult = document.getElementById('barResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(psiValue) || psiValue < 0) {
    barResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    barResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert PSI to bar (1 psi = 0.0689476 bar)
  const barValue = psiValue * 0.0689476;
  
  let formattedResult = barValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  barResult.value = formattedResult;
  barResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedPsi = psiValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedPsi} psi = ${formattedResult} bar`;
  liveConversion.classList.remove('hidden');
  
  psiInput.classList.add('border-indigo-500');
}

function copyResult() {
  const psiValue = document.getElementById('psiValue').value;
  const barResult = document.getElementById('barResult').value;
  const copyText = `${psiValue} psi = ${barResult} bar`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('psiValue').value = '';
  document.getElementById('barResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('barResult').classList.remove('result-highlight');
  document.getElementById('psiValue').classList.remove('border-indigo-500');
  document.getElementById('psiValue').focus();
}

document.getElementById('psiValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const psiInput = document.getElementById('psiValue');
  psiInput.focus();
  
  setTimeout(() => {
    psiInput.value = 30;
    updateConversion();
  }, 500);
});
</script>

