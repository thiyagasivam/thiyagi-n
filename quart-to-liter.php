<script>
let currentUnit = 'us'; // 'us' or 'uk'

function updateConversion() {
  const quartValue = parseFloat(document.getElementById('quartValue').value);
  const literResult = document.getElementById('literResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(quartValue) || quartValue < 0) {
    literResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert quarts to liters based on current unit type
  let literValue;
  if (currentUnit === 'us') {
    // 1 US quart = 0.946353 liters
    literValue = quartValue * 0.946353;
  } else {
    // 1 UK quart = 1.13652 liters
    literValue = quartValue * 1.13652;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (literValue >= 10) {
    formattedResult = literValue.toFixed(2);
  } else if (literValue >= 1) {
    formattedResult = literValue.toFixed(3);
  } else {
    formattedResult = literValue.toFixed(4);
  }
  
  literResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const quartValue = document.getElementById('quartValue').value;
  const literResult = document.getElementById('literResult').value;
  const unitType = currentUnit === 'us' ? 'US quarts' : 'UK quarts';
  const copyText = `${quartValue} ${unitType} = ${literResult} liters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usQuartBtn');
  const ukBtn = document.getElementById('ukQuartBtn');
  const quartLabel = document.getElementById('quartLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    quartLabel.textContent = 'US Quarts';
  } else {
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    quartLabel.textContent = 'UK Quarts';
  }
  
  updateConversion();
}

document.getElementById('quartValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usQuartBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('ukQuartBtn').addEventListener('click', () => switchUnit('uk'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('quartValue').focus();
  document.getElementById('quartValue').value = 1;
  updateConversion();
});
</script>

