<script>
function updateConversion() {
  const marlaValue = parseFloat(document.getElementById('marlaValue').value);
  const sqMResult = document.getElementById('sqMResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(marlaValue) || marlaValue < 0) {
    sqMResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert marla to square meters (1 marla = 25.2929 square meters)
  const sqMValue = marlaValue * 25.2929;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqMValue >= 1000) {
    formattedResult = sqMValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (sqMValue >= 100) {
    formattedResult = sqMValue.toFixed(2);
  } else {
    formattedResult = sqMValue.toFixed(3);
  }
  
  sqMResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const marlaValue = document.getElementById('marlaValue').value;
  const sqMResult = document.getElementById('sqMResult').value;
  const copyText = `${marlaValue} marla = ${sqMResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('marlaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('marlaValue').focus();
  document.getElementById('marlaValue').value = 5;
  updateConversion();
});
</script>

