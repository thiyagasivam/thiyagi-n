<script>
let isUpdating = false;

function convertLongCubitToFoot(longCubits) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(longCubits) && longCubits !== '') {
        const feet = longCubits * 1.75;
        document.getElementById('footInput').value = feet.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-amber-600 mr-2"></i>
            <strong>${longCubits} long cubit = ${feet.toFixed(6)} ft</strong>
        `;
    } else {
        document.getElementById('footInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertFootToLongCubit(feet) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(feet) && feet !== '') {
        const longCubits = feet / 1.75;
        document.getElementById('longCubitInput').value = longCubits.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-amber-600 mr-2"></i>
            <strong>${feet} ft = ${longCubits.toFixed(6)} long cubit</strong>
        `;
    } else {
        document.getElementById('longCubitInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('longCubitInput').value = '';
    document.getElementById('footInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const longCubitValue = document.getElementById('longCubitInput').value;
    const footValue = document.getElementById('footInput').value;
    
    document.getElementById('longCubitInput').value = footValue;
    document.getElementById('footInput').value = longCubitValue;
    
    if (footValue) {
        convertLongCubitToFoot(parseFloat(footValue));
    } else if (longCubitValue) {
        convertFootToLongCubit(parseFloat(longCubitValue));
    }
}

// Add event listeners
document.getElementById('longCubitInput').addEventListener('input', function() {
    convertLongCubitToFoot(parseFloat(this.value));
});

document.getElementById('footInput').addEventListener('input', function() {
    convertFootToLongCubit(parseFloat(this.value));
});
</script>

