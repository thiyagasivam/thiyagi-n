function updateConversion() {
  const tonValue = parseFloat(document.getElementById('tonValue').value);
  const lbsResult = document.getElementById('lbsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(tonValue) || tonValue < 0) {
    lbsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert tons to pounds (1 ton = 2000 lbs - US short ton)
  const lbsValue = tonValue * 2000;
  
  // Format the result
  let formattedResult;
  if (lbsValue >= 1000000) {
    formattedResult = lbsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (lbsValue >= 1000) {
    formattedResult = lbsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 2 
    });
  } else {
    formattedResult = lbsValue.toFixed(2);
  }
  
  lbsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const tonValue = document.getElementById('tonValue').value;
  const lbsResult = document.getElementById('lbsResult').value;
  const copyText = `${tonValue} tons = ${lbsResult} pounds`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('tonValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('tonValue').focus();
  document.getElementById('tonValue').value = 5;
  updateConversion();
});
</script>

