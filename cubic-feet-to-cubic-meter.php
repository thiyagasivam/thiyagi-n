<script>
function updateConversion() {
  const cuFtValue = parseFloat(document.getElementById('cuFtValue').value);
  const cuMResult = document.getElementById('cuMResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(cuFtValue) || cuFtValue < 0) {
    cuMResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cubic feet to cubic meters (1 cubic foot = 0.0283168 cubic meters)
  const cuMValue = cuFtValue * 0.0283168;
  
  // Format the result
  let formattedResult;
  if (cuMValue >= 1000) {
    formattedResult = cuMValue.toFixed(2);
  } else if (cuMValue >= 1) {
    formattedResult = cuMValue.toFixed(4);
  } else {
    formattedResult = cuMValue.toFixed(6);
  }
  
  cuMResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const cuFtValue = document.getElementById('cuFtValue').value;
  const cuMResult = document.getElementById('cuMResult').value;
  const copyText = `${cuFtValue} cubic feet = ${cuMResult} cubic meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('cuFtValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('cuFtValue').focus();
  document.getElementById('cuFtValue').value = 35.31;
  updateConversion();
});
</script>

