<script>
function updateConversion() {
  const bighaValue = parseFloat(document.getElementById('bighaValue').value);
  const hectareResult = document.getElementById('hectareResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(bighaValue) || bighaValue < 0) {
    hectareResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert bigha to hectares (1 bigha = 0.2529 hectares - standard conversion)
  const hectareValue = bighaValue * 0.2529;
  
  // Format the result
  let formattedResult;
  if (hectareValue >= 100) {
    formattedResult = hectareValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (hectareValue >= 1) {
    formattedResult = hectareValue.toFixed(3);
  } else {
    formattedResult = hectareValue.toFixed(4);
  }
  
  hectareResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const bighaValue = document.getElementById('bighaValue').value;
  const hectareResult = document.getElementById('hectareResult').value;
  const copyText = `${bighaValue} bigha = ${hectareResult} hectares`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('bighaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('bighaValue').focus();
  document.getElementById('bighaValue').value = 4;
  updateConversion();
});
</script>

