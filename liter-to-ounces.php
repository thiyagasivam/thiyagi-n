function updateConversion() {
  const literValue = parseFloat(document.getElementById('literValue').value);
  const ouncesResult = document.getElementById('ouncesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(literValue) || literValue < 0) {
    ouncesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert liters to fluid ounces (1 liter = 33.8140227 fl oz)
  const ouncesValue = literValue * 33.8140227;
  
  // Format the result
  let formattedResult;
  if (ouncesValue >= 1000) {
    formattedResult = ouncesValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (ouncesValue >= 1) {
    formattedResult = ouncesValue.toFixed(2);
  } else {
    formattedResult = ouncesValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  ouncesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const literValue = document.getElementById('literValue').value;
  const ouncesResult = document.getElementById('ouncesResult').value;
  const copyText = `${literValue} liters = ${ouncesResult} fluid ounces`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('literValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('literValue').focus();
  document.getElementById('literValue').value = 1;
  updateConversion();
});
</script>

