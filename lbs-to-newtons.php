function updateConversion() {
  const poundsValue = parseFloat(document.getElementById('poundsValue').value);
  const newtonsResult = document.getElementById('newtonsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(poundsValue) || poundsValue < 0) {
    newtonsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert pounds to newtons (1 lbf = 4.44822162 N)
  const newtonsValue = poundsValue * 4.44822162;
  
  // Format the result
  let formattedResult;
  if (newtonsValue >= 1000) {
    formattedResult = newtonsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (newtonsValue >= 1) {
    formattedResult = newtonsValue.toFixed(2);
  } else {
    formattedResult = newtonsValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  newtonsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const poundsValue = document.getElementById('poundsValue').value;
  const newtonsResult = document.getElementById('newtonsResult').value;
  const copyText = `${poundsValue} lbf = ${newtonsResult} N`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('poundsValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('poundsValue').focus();
  document.getElementById('poundsValue').value = 10;
  updateConversion();
});
</script>

