<script>
function updateConversion() {
  const sqmValue = parseFloat(document.getElementById('sqmValue').value);
  const sqinResult = document.getElementById('sqinResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqmValue) || sqmValue < 0) {
    sqinResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square meters to square inches
  // 1 square meter = 1550.0031 square inches
  const sqinValue = sqmValue * 1550.0031;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqinValue >= 10000) {
    formattedResult = sqinValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (sqinValue >= 1000) {
    formattedResult = sqinValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (sqinValue >= 100) {
    formattedResult = sqinValue.toFixed(1);
  } else if (sqinValue >= 10) {
    formattedResult = sqinValue.toFixed(2);
  } else {
    formattedResult = sqinValue.toFixed(3);
  }
  
  sqinResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqmValue = document.getElementById('sqmValue').value;
  const sqinResult = document.getElementById('sqinResult').value;
  const copyText = `${sqmValue} square meters = ${sqinResult} square inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqmValue').focus();
  document.getElementById('sqmValue').value = 1;
  updateConversion();
});
</script>

