  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(inputValue)) {
    resultField.value = '';
    copyBtn.classList.add('hidden');
    updateURL('', '', '');
    return;
  }
  
  // Convert input to m/s² first, then to target unit
  const valueInMS2 = inputValue * conversionFactors[fromUnit];
  const result = valueInMS2 / conversionFactors[toUnit];
  
  // Format the result
  let formattedResult;
  if (Math.abs(result) >= 1000) {
    formattedResult = result.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (Math.abs(result) >= 1) {
    formattedResult = result.toFixed(4);
  } else if (Math.abs(result) >= 0.001) {
    formattedResult = result.toFixed(6);
  } else {
    formattedResult = result.toExponential(3);
  }
  
  resultField.value = formattedResult;
  copyBtn.classList.remove('hidden');
  
  // Update URL for better SEO and sharing
  updateURL(inputValue, fromUnit, toUnit);
}

function updateURL(value, from, to) {
  if (!value || !from || !to) {
    // Clear URL parameters if no valid conversion
    const url = new URL(window.location);
    url.searchParams.delete('value');
    url.searchParams.delete('from');
    url.searchParams.delete('to');
    window.history.replaceState({}, '', url);
    return;
  }
  
  // Update URL with conversion parameters
  const url = new URL(window.location);
  url.searchParams.set('value', value);
  url.searchParams.set('from', from);
  url.searchParams.set('to', to);
  window.history.replaceState({}, '', url);
}

function copyResult() {
  const inputValue = document.getElementById('accelerationValue').value;
  const fromUnit = document.getElementById('fromUnit').value;
  const toUnit = document.getElementById('toUnit').value;
  const resultValue = document.getElementById('resultValue').value;
  
  const fromUnitText = document.getElementById('fromUnit').options[document.getElementById('fromUnit').selectedIndex].text;
  const toUnitText = document.getElementById('toUnit').options[document.getElementById('toUnit').selectedIndex].text;
  
  const copyText = `${inputValue} ${fromUnitText} = ${resultValue} ${toUnitText}`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
    
    // Track copy action for analytics
    if (typeof gtag !== 'undefined') {
      gtag('event', 'copy_result', {
        'event_category': 'converter',
        'event_label': 'acceleration_converter',
        'value': 1
      });
    }
  }).catch(err => {
    console.error('Failed to copy: ', err);
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function loadFromURL() {
  const url = new URL(window.location);
  const value = url.searchParams.get('value');
  const from = url.searchParams.get('from');
  const to = url.searchParams.get('to');
  
  if (value && from && to) {
    document.getElementById('accelerationValue').value = value;
    document.getElementById('fromUnit').value = from;
    document.getElementById('toUnit').value = to;
    updateConversion();
  }
}

function swapUnits() {
  const fromSelect = document.getElementById('fromUnit');
  const toSelect = document.getElementById('toUnit');
  
  const fromValue = fromSelect.value;
  const toValue = toSelect.value;
  
  fromSelect.value = toValue;
  toSelect.value = fromValue;
  
  updateConversion();
}

// Enhanced keyboard shortcuts
function handleKeyboardShortcuts(event) {
  // Ctrl/Cmd + Enter to copy result
  if ((event.ctrlKey || event.metaKey) && event.key === 'Enter') {
    event.preventDefault();
    if (!document.getElementById('copyBtn').classList.contains('hidden')) {
      copyResult();
    }
  }
  
  // Escape to clear input
  if (event.key === 'Escape') {
    document.getElementById('accelerationValue').value = '';
    updateConversion();
  }
  
  // Ctrl/Cmd + S to swap units
  if ((event.ctrlKey || event.metaKey) && event.key === 's') {
    event.preventDefault();
    swapUnits();
  }
}

