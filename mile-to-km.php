<script>
function updateConversion() {
  const mileValue = parseFloat(document.getElementById('mileValue').value);
  const kmResult = document.getElementById('kmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mileValue) || mileValue < 0) {
    kmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert miles to kilometers (1 mile = 1.6093 kilometers)
  const kmValue = mileValue * 1.6093;
  
  // Format the result
  let formattedResult;
  if (kmValue >= 1000) {
    formattedResult = kmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (kmValue >= 10) {
    formattedResult = kmValue.toFixed(2);
  } else {
    formattedResult = kmValue.toFixed(4);
  }
  
  kmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mileValue = document.getElementById('mileValue').value;
  const kmResult = document.getElementById('kmResult').value;
  const copyText = `${mileValue} miles = ${kmResult} kilometers`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('mileValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mileValue').focus();
  document.getElementById('mileValue').value = 100;
  updateConversion();
});
</script>

