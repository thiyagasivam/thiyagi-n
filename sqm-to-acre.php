<script>
function updateConversion() {
  const sqmValue = parseFloat(document.getElementById('sqmValue').value);
  const acreResult = document.getElementById('acreResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqmValue) || sqmValue < 0) {
    acreResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square meters to acres (1 acre = 4046.8564224 sqm)
  const acreValue = sqmValue / 4046.8564224;
  
  // Format the result
  let formattedResult;
  if (acreValue >= 1) {
    formattedResult = acreValue.toFixed(2);
  } else {
    formattedResult = acreValue.toFixed(4);
  }
  
  acreResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqmValue = document.getElementById('sqmValue').value;
  const acreResult = document.getElementById('acreResult').value;
  const copyText = `${sqmValue} square meters = ${acreResult} acres`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqmValue').focus();
  document.getElementById('sqmValue').value = 4047;
  updateConversion();
});
</script>

