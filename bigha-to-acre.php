<script>
  // Conversion function: 1 bigha = 0.6198 acres (standard Indian conversion)
  function convertBighaToAcre() {
    const bighaInput = document.getElementById('bighaValue');
    const acreResult = document.getElementById('acreResult');
    const copyBtn = document.getElementById('copyBtn');
    
    const bighaValue = parseFloat(bighaInput.value);
    
    if (isNaN(bighaValue) || bighaValue < 0) {
      acreResult.value = '';
      copyBtn.classList.add('hidden');
      return;
    }
    
    const acreValue = bighaValue * 0.6198;
    acreResult.value = acreValue.toFixed(6);
    copyBtn.classList.remove('hidden');
  }
  
  // Copy to clipboard function
  function copyResult() {
    const acreResult = document.getElementById('acreResult');
    const copySuccess = document.getElementById('copySuccess');
    
    if (acreResult.value) {
      navigator.clipboard.writeText(acreResult.value).then(() => {
        copySuccess.classList.remove('hidden');
        setTimeout(() => {
          copySuccess.classList.add('hidden');
        }, 2000);
      });
    }
  }
  
  // Event listeners
  document.getElementById('bighaValue').addEventListener('input', convertBighaToAcre);
  document.getElementById('copyBtn').addEventListener('click', copyResult);
  
  // Initialize conversion
  convertBighaToAcre();
</script>

