<script>
function updateConversion() {
  const mgInput = document.getElementById('mgValue');
  const mgValue = parseFloat(mgInput.value);
  const ugResult = document.getElementById('ugResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(mgValue) || mgValue < 0) {
    ugResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    ugResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert milligrams to micrograms (1 mg = 1000 µg)
  const ugValue = mgValue * 1000;
  
  let formattedResult = ugValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 3 
  });
  
  ugResult.value = formattedResult;
  ugResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMg = mgValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMg} mg = ${formattedResult} µg`;
  liveConversion.classList.remove('hidden');
  
  mgInput.classList.add('border-emerald-500');
}

function copyResult() {
  const mgValue = document.getElementById('mgValue').value;
  const ugResult = document.getElementById('ugResult').value;
  const copyText = `${mgValue} mg = ${ugResult} µg`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('mgValue').value = '';
  document.getElementById('ugResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('ugResult').classList.remove('result-highlight');
  document.getElementById('mgValue').classList.remove('border-emerald-500');
  document.getElementById('mgValue').focus();
}

document.getElementById('mgValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const mgInput = document.getElementById('mgValue');
  mgInput.focus();
  
  setTimeout(() => {
    mgInput.value = 2.5;
    updateConversion();
  }, 500);
});
</script>

