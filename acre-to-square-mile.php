<script>
function updateConversion() {
  const acreValue = parseFloat(document.getElementById('acreValue').value);
  const sqMileResult = document.getElementById('sqMileResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(acreValue) || acreValue < 0) {
    sqMileResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert acres to square miles (640 acres = 1 square mile)
  const sqMileValue = acreValue / 640;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqMileValue >= 10) {
    formattedResult = sqMileValue.toFixed(2);
  } else if (sqMileValue >= 1) {
    formattedResult = sqMileValue.toFixed(3);
  } else {
    formattedResult = sqMileValue.toFixed(5);
  }
  
  sqMileResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const acreValue = document.getElementById('acreValue').value;
  const sqMileResult = document.getElementById('sqMileResult').value;
  const copyText = `${acreValue} acres = ${sqMileResult} square miles`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('acreValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('acreValue').focus();
  document.getElementById('acreValue').value = 640;
  updateConversion();
});
</script>

