<script>
function updateConversion() {
  const kilogramInput = document.getElementById('kilogramValue');
  const kilogramValue = parseFloat(kilogramInput.value);
  const ounceResult = document.getElementById('ounceResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(kilogramValue) || kilogramValue < 0) {
    ounceResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    ounceResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert kilograms to ounces (1 kg = 35.274 ounces)
  const ounceValue = kilogramValue * 35.274;
  
  let formattedResult = ounceValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  ounceResult.value = formattedResult;
  ounceResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedKilogram = kilogramValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedKilogram} kg = ${formattedResult} oz`;
  liveConversion.classList.remove('hidden');
  
  kilogramInput.classList.add('border-emerald-500');
}

function copyResult() {
  const kilogramValue = document.getElementById('kilogramValue').value;
  const ounceResult = document.getElementById('ounceResult').value;
  const copyText = `${kilogramValue} kg = ${ounceResult} oz`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('kilogramValue').value = '';
  document.getElementById('ounceResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('ounceResult').classList.remove('result-highlight');
  document.getElementById('kilogramValue').classList.remove('border-emerald-500');
  document.getElementById('kilogramValue').focus();
}

document.getElementById('kilogramValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const kilogramInput = document.getElementById('kilogramValue');
  kilogramInput.focus();
  
  setTimeout(() => {
    kilogramInput.value = 1;
    updateConversion();
  }, 500);
});
</script>

