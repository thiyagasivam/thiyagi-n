<script>
function updateConversion() {
  const celsiusValue = parseFloat(document.getElementById('celsiusValue').value);
  const kelvinResult = document.getElementById('kelvinResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(celsiusValue)) {
    kelvinResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert Celsius to Kelvin: K = °C + 273.15
  const kelvinValue = celsiusValue + 273.15;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (Math.abs(kelvinValue) >= 1000) {
    formattedResult = kelvinValue.toFixed(1);
  } else if (Math.abs(kelvinValue) >= 100) {
    formattedResult = kelvinValue.toFixed(2);
  } else {
    formattedResult = kelvinValue.toFixed(3);
  }
  
  kelvinResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const celsiusValue = document.getElementById('celsiusValue').value;
  const kelvinResult = document.getElementById('kelvinResult').value;
  const copyText = `${celsiusValue}°C = ${kelvinResult} K`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('celsiusValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('celsiusValue').focus();
  document.getElementById('celsiusValue').value = 25;
  updateConversion();
});
</script>

