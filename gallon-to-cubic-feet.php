<script>
function updateConversion() {
  const gallonInput = document.getElementById('gallonValue');
  const gallonValue = parseFloat(gallonInput.value);
  const cubicFeetResult = document.getElementById('cubicFeetResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(gallonValue) || gallonValue < 0) {
    cubicFeetResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    cubicFeetResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert gallons to cubic feet (1 gallon = 0.133681 cubic feet)
  const cubicFeetValue = gallonValue * 0.133681;
  
  let formattedResult = cubicFeetValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  cubicFeetResult.value = formattedResult;
  cubicFeetResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedGallon = gallonValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedGallon} gal = ${formattedResult} ft³`;
  liveConversion.classList.remove('hidden');
  
  gallonInput.classList.add('border-violet-500');
}

function copyResult() {
  const gallonValue = document.getElementById('gallonValue').value;
  const cubicFeetResult = document.getElementById('cubicFeetResult').value;
  const copyText = `${gallonValue} gal = ${cubicFeetResult} ft³`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('gallonValue').value = '';
  document.getElementById('cubicFeetResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('cubicFeetResult').classList.remove('result-highlight');
  document.getElementById('gallonValue').classList.remove('border-violet-500');
  document.getElementById('gallonValue').focus();
}

document.getElementById('gallonValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const gallonInput = document.getElementById('gallonValue');
  gallonInput.focus();
  
  setTimeout(() => {
    gallonInput.value = 50;
    updateConversion();
  }, 500);
});
</script>

