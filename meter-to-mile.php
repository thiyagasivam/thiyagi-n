<script>
function updateConversion() {
  const meterValue = parseFloat(document.getElementById('meterValue').value);
  const mileResult = document.getElementById('mileResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(meterValue) || meterValue < 0) {
    mileResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert meters to miles (1 meter = 0.000621371 miles)
  const mileValue = meterValue / 1609.344;
  
  // Format the result
  let formattedResult;
  if (mileValue >= 100) {
    formattedResult = mileValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (mileValue >= 10) {
    formattedResult = mileValue.toFixed(3);
  } else if (mileValue >= 1) {
    formattedResult = mileValue.toFixed(4);
  } else {
    formattedResult = mileValue.toFixed(6);
  }
  
  mileResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const meterValue = document.getElementById('meterValue').value;
  const mileResult = document.getElementById('mileResult').value;
  const copyText = `${meterValue} meters = ${mileResult} miles`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('meterValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('meterValue').focus();
  document.getElementById('meterValue').value = 1609.344;
  updateConversion();
});
</script>

