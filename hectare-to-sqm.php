<script>
function updateConversion() {
  const hectareValue = parseFloat(document.getElementById('hectareValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(hectareValue) || hectareValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert hectares to square meters (1 hectare = 10,000 square meters)
  const sqmValue = hectareValue * 10000;
  
  // Format the result
  let formattedResult;
  if (sqmValue >= 1000000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqmValue >= 1000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else {
    formattedResult = sqmValue.toFixed(2);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const hectareValue = document.getElementById('hectareValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${hectareValue} hectares = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('hectareValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('hectareValue').focus();
  document.getElementById('hectareValue').value = 2;
  updateConversion();
});
</script>

