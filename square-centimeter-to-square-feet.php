<script>
function updateConversion() {
  const sqCmValue = parseFloat(document.getElementById('sqCmValue').value);
  const sqFtResult = document.getElementById('sqFtResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqCmValue) || sqCmValue < 0) {
    sqFtResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square centimeters to square feet (929.0304 sq cm = 1 sq ft)
  const sqFtValue = sqCmValue / 929.0304;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqFtValue >= 100) {
    formattedResult = sqFtValue.toFixed(2);
  } else if (sqFtValue >= 10) {
    formattedResult = sqFtValue.toFixed(3);
  } else if (sqFtValue >= 1) {
    formattedResult = sqFtValue.toFixed(4);
  } else {
    formattedResult = sqFtValue.toFixed(6);
  }
  
  sqFtResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqCmValue = document.getElementById('sqCmValue').value;
  const sqFtResult = document.getElementById('sqFtResult').value;
  const copyText = `${sqCmValue} square centimeters = ${sqFtResult} square feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqCmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqCmValue').focus();
  document.getElementById('sqCmValue').value = 929;
  updateConversion();
});
</script>

