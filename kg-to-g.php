// Enhanced conversion function
function updateConversion() {
  const kgInput = document.getElementById('kgValue');
  const kgValue = parseFloat(kgInput.value);
  const gramResult = document.getElementById('gramResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(kgValue) || kgValue < 0) {
    gramResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    gramResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert kg to grams (1 kg = 1000 g)
  const gramValue = kgValue * 1000;
  
  // Format result
  let formattedResult = gramValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  // Update result field
  gramResult.value = formattedResult;
  gramResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedKG = kgValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedKG} kg = ${formattedResult} g`;
  liveConversion.classList.remove('hidden');
  
  kgInput.classList.add('border-emerald-500');
}

// Copy function
function copyResult() {
  const kgValue = document.getElementById('kgValue').value;
  const gramResult = document.getElementById('gramResult').value;
  const copyText = `${kgValue} kg = ${gramResult} g`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('kgValue').value = '';
  document.getElementById('gramResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('gramResult').classList.remove('result-highlight');
  document.getElementById('kgValue').classList.remove('border-emerald-500');
  document.getElementById('kgValue').focus();
}

// Event listeners
document.getElementById('kgValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Auto-focus and demo value on page load
window.addEventListener('DOMContentLoaded', () => {
  const kgInput = document.getElementById('kgValue');
  kgInput.focus();
  
  setTimeout(() => {
    kgInput.value = 2.5;
    updateConversion();
  }, 500);
});
</script>

