<script>
function updateConversion() {
  const centValue = parseFloat(document.getElementById('centValue').value);
  const acreResult = document.getElementById('acreResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(centValue) || centValue < 0) {
    acreResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cent to acres (100 cents = 1 acre)
  const acreValue = centValue / 100;
  
  // Format the result
  let formattedResult;
  if (acreValue >= 10) {
    formattedResult = acreValue.toFixed(1);
  } else if (acreValue >= 1) {
    formattedResult = acreValue.toFixed(2);
  } else {
    formattedResult = acreValue.toFixed(3);
  }
  
  acreResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const centValue = document.getElementById('centValue').value;
  const acreResult = document.getElementById('acreResult').value;
  const copyText = `${centValue} cents = ${acreResult} acres`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('centValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('centValue').focus();
  document.getElementById('centValue').value = 100;
  updateConversion();
});
</script>

