<script>
function updateConversion() {
  const newtonInput = document.getElementById('newtonValue');
  const newtonValue = parseFloat(newtonInput.value);
  const kilogramResult = document.getElementById('kilogramResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(newtonValue) || newtonValue < 0) {
    kilogramResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    kilogramResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert newtons to kilograms (1 N = 0.101972 kg - using standard gravity)
  const kilogramValue = newtonValue / 9.80665;
  
  let formattedResult = kilogramValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  kilogramResult.value = formattedResult;
  kilogramResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedNewton = newtonValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedNewton} N = ${formattedResult} kg`;
  liveConversion.classList.remove('hidden');
  
  newtonInput.classList.add('border-violet-500');
}

function copyResult() {
  const newtonValue = document.getElementById('newtonValue').value;
  const kilogramResult = document.getElementById('kilogramResult').value;
  const copyText = `${newtonValue} N = ${kilogramResult} kg`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('newtonValue').value = '';
  document.getElementById('kilogramResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('kilogramResult').classList.remove('result-highlight');
  document.getElementById('newtonValue').classList.remove('border-violet-500');
  document.getElementById('newtonValue').focus();
}

document.getElementById('newtonValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const newtonInput = document.getElementById('newtonValue');
  newtonInput.focus();
  
  setTimeout(() => {
    newtonInput.value = 98.0665;
    updateConversion();
  }, 500);
});
</script>

