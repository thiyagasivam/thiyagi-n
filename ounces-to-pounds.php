function updateConversion() {
  const ouncesValue = parseFloat(document.getElementById('ouncesValue').value);
  const poundsResult = document.getElementById('poundsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(ouncesValue) || ouncesValue < 0) {
    poundsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert ounces to pounds (1 ounce = 0.0625 pounds)
  const poundsValue = ouncesValue * 0.0625;
  
  // Format the result
  let formattedResult;
  if (poundsValue >= 1) {
    formattedResult = poundsValue.toFixed(3);
  } else if (poundsValue >= 0.001) {
    formattedResult = poundsValue.toFixed(4);
  } else {
    formattedResult = poundsValue.toExponential(3);
  }
  
  // Remove trailing zeros
  if (poundsValue >= 0.001) {
    formattedResult = parseFloat(formattedResult).toString();
  }
  
  poundsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const ouncesValue = document.getElementById('ouncesValue').value;
  const poundsResult = document.getElementById('poundsResult').value;
  const copyText = `${ouncesValue} ounces = ${poundsResult} pounds`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('ouncesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('ouncesValue').focus();
  document.getElementById('ouncesValue').value = 16;
  updateConversion();
});
</script>

