// Enhanced conversion function with advanced features
function updateConversion() {
  const quartInput = document.getElementById('quartValue');
  const quartValue = parseFloat(quartInput.value);
  const literResult = document.getElementById('literResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(quartValue) || quartValue < 0) {
    literResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    literResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert quarts to liters (1 US quart = 0.946353 liters)
  const literValue = quartValue * 0.946353;
  
  // Advanced formatting based on value size
  let formattedResult;
  if (literValue >= 1000000) {
    formattedResult = literValue.toExponential(3);
  } else if (literValue >= 1000) {
    formattedResult = literValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 2 
    });
  } else if (literValue >= 100) {
    formattedResult = literValue.toFixed(3);
  } else if (literValue >= 10) {
    formattedResult = literValue.toFixed(4);
  } else if (literValue >= 1) {
    formattedResult = literValue.toFixed(5);
  } else {
    formattedResult = literValue.toFixed(6);
  }
  
  // Update result field
  literResult.value = formattedResult;
  literResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedQuarts = quartValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedQuarts} qt = ${formattedResult} L`;
  liveConversion.classList.remove('hidden');
  
  // Add visual feedback
  quartInput.classList.add('border-blue-500');
}

// Enhanced copy function
function copyResult() {
  const quartValue = document.getElementById('quartValue').value;
  const literResult = document.getElementById('literResult').value;
  const copyText = `${quartValue} quarts = ${literResult} liters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('quartValue').value = '';
  document.getElementById('literResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('literResult').classList.remove('result-highlight');
  document.getElementById('quartValue').classList.remove('border-blue-500');
  document.getElementById('quartValue').focus();
}

// Event listeners
document.getElementById('quartValue').addEventListener('input', updateConversion);
document.getElementById('quartValue').addEventListener('keypress', function(e) {
  if (e.key === 'Enter') {
    this.blur();
    if (this.value) {
      document.getElementById('copyBtn').focus();
    }
  }
});

document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Keyboard shortcuts
document.addEventListener('keydown', function(e) {
  if (e.ctrlKey && e.key === 'c' && !document.getElementById('copyBtn').classList.contains('hidden')) {
    e.preventDefault();
    copyResult();
  }
  if (e.key === 'Escape') {
    clearAll();
  }
});

