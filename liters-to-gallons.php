function updateConversion() {
  const litersValue = parseFloat(document.getElementById('litersValue').value);
  const gallonsResult = document.getElementById('gallonsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(litersValue) || litersValue < 0) {
    gallonsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert liters to gallons (1 L = 0.264172 US gallons)
  const gallonsValue = litersValue * 0.264172;
  
  // Format the result
  let formattedResult;
  if (gallonsValue >= 1000) {
    formattedResult = gallonsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (gallonsValue >= 1) {
    formattedResult = gallonsValue.toFixed(4);
  } else {
    formattedResult = gallonsValue.toFixed(6);
  }
  
  gallonsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const litersValue = document.getElementById('litersValue').value;
  const gallonsResult = document.getElementById('gallonsResult').value;
  const copyText = `${litersValue} liters = ${gallonsResult} gallons`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('litersValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('litersValue').focus();
  document.getElementById('litersValue').value = 50;
  updateConversion();
});
</script>

