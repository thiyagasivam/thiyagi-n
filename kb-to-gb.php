<script>
function updateConversion() {
  const kbInput = document.getElementById('kbValue');
  const kbValue = parseFloat(kbInput.value);
  const gbResult = document.getElementById('gbResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(kbValue) || kbValue < 0) {
    gbResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    gbResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert KB to GB (1 KB = 0.000001 GB or 1 GB = 1,000,000 KB)
  const gbValue = kbValue / 1000000;
  
  let formattedResult;
  if (gbValue >= 1) {
    formattedResult = gbValue.toLocaleString('en-US', { 
      minimumFractionDigits: 3, 
      maximumFractionDigits: 6 
    });
  } else {
    formattedResult = gbValue.toFixed(9);
  }
  
  gbResult.value = formattedResult;
  gbResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedKB = kbValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedKB} KB = ${formattedResult} GB`;
  liveConversion.classList.remove('hidden');
  
  kbInput.classList.add('border-amber-500');
}

function copyResult() {
  const kbValue = document.getElementById('kbValue').value;
  const gbResult = document.getElementById('gbResult').value;
  const copyText = `${kbValue} KB = ${gbResult} GB`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('kbValue').value = '';
  document.getElementById('gbResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('gbResult').classList.remove('result-highlight');
  document.getElementById('kbValue').classList.remove('border-amber-500');
  document.getElementById('kbValue').focus();
}

document.getElementById('kbValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const kbInput = document.getElementById('kbValue');
  kbInput.focus();
  
  setTimeout(() => {
    kbInput.value = 1000000;
    updateConversion();
  }, 500);
});
</script>

