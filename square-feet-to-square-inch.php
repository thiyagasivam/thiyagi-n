<script>
function updateConversion() {
  const sqFeetValue = parseFloat(document.getElementById('sqFeetValue').value);
  const sqInchResult = document.getElementById('sqInchResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqFeetValue) || sqFeetValue < 0) {
    sqInchResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square feet to square inches (1 square foot = 144 square inches)
  const sqInchValue = sqFeetValue * 144;
  
  // Format the result
  let formattedResult;
  if (sqInchValue >= 10000) {
    formattedResult = sqInchValue.toLocaleString();
  } else if (sqInchValue >= 1000) {
    formattedResult = sqInchValue.toFixed(1);
  } else {
    formattedResult = sqInchValue.toFixed(2);
  }
  
  sqInchResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqFeetValue = document.getElementById('sqFeetValue').value;
  const sqInchResult = document.getElementById('sqInchResult').value;
  const copyText = `${sqFeetValue} square feet = ${sqInchResult} square inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqFeetValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqFeetValue').focus();
  document.getElementById('sqFeetValue').value = 1;
  updateConversion();
});
</script>

