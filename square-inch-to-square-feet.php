<script>
function updateConversion() {
  const sqInchValue = parseFloat(document.getElementById('sqInchValue').value);
  const sqFeetResult = document.getElementById('sqFeetResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqInchValue) || sqInchValue < 0) {
    sqFeetResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square inches to square feet (1 square foot = 144 square inches)
  const sqFeetValue = sqInchValue / 144;
  
  // Format the result
  let formattedResult;
  if (sqFeetValue >= 10) {
    formattedResult = sqFeetValue.toFixed(4);
  } else if (sqFeetValue >= 1) {
    formattedResult = sqFeetValue.toFixed(5);
  } else {
    formattedResult = sqFeetValue.toFixed(6);
  }
  
  sqFeetResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqInchValue = document.getElementById('sqInchValue').value;
  const sqFeetResult = document.getElementById('sqFeetResult').value;
  const copyText = `${sqInchValue} square inches = ${sqFeetResult} square feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqInchValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqInchValue').focus();
  document.getElementById('sqInchValue').value = 144;
  updateConversion();
});
</script>

