<script>
function updateConversion() {
  const yardValue = parseFloat(document.getElementById('yardValue').value);
  const meterResult = document.getElementById('meterResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(yardValue) || yardValue < 0) {
    meterResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert yards to meters (1 yard = 0.9144 meters)
  const meterValue = yardValue * 0.9144;
  
  // Format the result
  let formattedResult;
  if (meterValue >= 1000) {
    formattedResult = meterValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (meterValue >= 10) {
    formattedResult = meterValue.toFixed(2);
  } else {
    formattedResult = meterValue.toFixed(4);
  }
  
  meterResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const yardValue = document.getElementById('yardValue').value;
  const meterResult = document.getElementById('meterResult').value;
  const copyText = `${yardValue} yards = ${meterResult} meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('yardValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('yardValue').focus();
  document.getElementById('yardValue').value = 100;
  updateConversion();
});
</script>

