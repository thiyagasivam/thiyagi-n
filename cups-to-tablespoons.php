<script>
function updateConversion() {
  const cupInput = document.getElementById('cupValue');
  const cupValue = parseFloat(cupInput.value);
  const tablespoonResult = document.getElementById('tablespoonResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(cupValue) || cupValue < 0) {
    tablespoonResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    tablespoonResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert cups to tablespoons (1 US cup = 16 tablespoons)
  const tablespoonValue = cupValue * 16;
  
  let formattedResult = tablespoonValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 4 
  });
  
  tablespoonResult.value = formattedResult;
  tablespoonResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedCup = cupValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedCup} cup = ${formattedResult} tbsp`;
  liveConversion.classList.remove('hidden');
  
  cupInput.classList.add('border-sky-500');
}

function copyResult() {
  const cupValue = document.getElementById('cupValue').value;
  const tablespoonResult = document.getElementById('tablespoonResult').value;
  const copyText = `${cupValue} cup = ${tablespoonResult} tbsp`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('cupValue').value = '';
  document.getElementById('tablespoonResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('tablespoonResult').classList.remove('result-highlight');
  document.getElementById('cupValue').classList.remove('border-sky-500');
  document.getElementById('cupValue').focus();
}

document.getElementById('cupValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const cupInput = document.getElementById('cupValue');
  cupInput.focus();
  
  setTimeout(() => {
    cupInput.value = 1;
    updateConversion();
  }, 500);
});
</script>

