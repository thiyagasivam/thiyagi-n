<script>
let isUpdating = false;

function convertGreekCubitToFoot(greekCubit) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(greekCubit) && greekCubit !== '') {
        // 1 Greek cubit = 1.476 feet (approximately 18 inches)
        const foot = greekCubit * 1.476;
        document.getElementById('footInput').value = foot.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-amber-600 mr-2"></i>
            <strong>${greekCubit} Greek cubits = ${foot.toFixed(3)} feet</strong>
        `;
    } else {
        document.getElementById('footInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertFootToGreekCubit(foot) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(foot) && foot !== '') {
        // 1 foot = 1/1.476 Greek cubits
        const greekCubit = foot / 1.476;
        document.getElementById('greekCubitInput').value = greekCubit.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-amber-600 mr-2"></i>
            <strong>${foot} feet = ${greekCubit.toFixed(3)} Greek cubits</strong>
        `;
    } else {
        document.getElementById('greekCubitInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('greekCubitInput').value = '';
    document.getElementById('footInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const greekCubitValue = document.getElementById('greekCubitInput').value;
    const footValue = document.getElementById('footInput').value;
    
    document.getElementById('greekCubitInput').value = footValue;
    document.getElementById('footInput').value = greekCubitValue;
    
    if (footValue) {
        convertGreekCubitToFoot(parseFloat(footValue));
    } else if (greekCubitValue) {
        convertFootToGreekCubit(parseFloat(greekCubitValue));
    }
}

// Add event listeners
document.getElementById('greekCubitInput').addEventListener('input', function() {
    convertGreekCubitToFoot(parseFloat(this.value));
});

document.getElementById('footInput').addEventListener('input', function() {
    convertFootToGreekCubit(parseFloat(this.value));
});
</script>

