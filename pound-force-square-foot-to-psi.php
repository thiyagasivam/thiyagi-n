<script>
let isUpdating = false;

function convertPSFToPSI(psf) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(psf) && psf !== '') {
        const psi = psf / 144;
        document.getElementById('psiInput').value = psi.toFixed(8);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-emerald-600 mr-2"></i>
            <strong>${psf} psf = ${psi.toFixed(8)} psi</strong>
        `;
    } else {
        document.getElementById('psiInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertPSIToPSF(psi) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(psi) && psi !== '') {
        const psf = psi * 144;
        document.getElementById('psfInput').value = psf.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-emerald-600 mr-2"></i>
            <strong>${psi} psi = ${psf.toFixed(6)} psf</strong>
        `;
    } else {
        document.getElementById('psfInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('psfInput').value = '';
    document.getElementById('psiInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const psfValue = document.getElementById('psfInput').value;
    const psiValue = document.getElementById('psiInput').value;
    
    document.getElementById('psfInput').value = psiValue;
    document.getElementById('psiInput').value = psfValue;
    
    if (psiValue) {
        convertPSFToPSI(parseFloat(psiValue));
    } else if (psfValue) {
        convertPSIToPSF(parseFloat(psfValue));
    }
}

// Add event listeners
document.getElementById('psfInput').addEventListener('input', function() {
    convertPSFToPSI(parseFloat(this.value));
});

document.getElementById('psiInput').addEventListener('input', function() {
    convertPSIToPSF(parseFloat(this.value));
});
</script>

