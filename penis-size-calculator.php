                if (percentile < 5) return { name: 'Below average', color: 'text-red-600' };
                if (percentile < 25) return { name: 'Lower average', color: 'text-orange-600' };
                if (percentile < 75) return { name: 'Average', color: 'text-green-600' };
                if (percentile < 95) return { name: 'Above average', color: 'text-blue-600' };
                return { name: 'Well above average', color: 'text-purple-600' };
            }

