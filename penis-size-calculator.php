            calculateStatistics() {
                const length = parseFloat(document.getElementById('length').value);
                const circumference = parseFloat(document.getElementById('circumference').value);
                const age = document.getElementById('age').value;

                if (!length || !circumference) {
                    alert('Please enter both length and circumference measurements.');
                    return;
                }

                if (length < 5 || length > 25 || circumference < 5 || circumference > 20) {
                    alert('Please enter realistic measurements within the valid ranges.');
                    return;
                }

                // Calculate percentiles
                const lengthPercentile = this.calculatePercentile(length, this.medicalData.length.mean, this.medicalData.length.stdDev);
                const circumferencePercentile = this.calculatePercentile(circumference, this.medicalData.circumference.mean, this.medicalData.circumference.stdDev);

