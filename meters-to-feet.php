function updateConversion() {
  const metersValue = parseFloat(document.getElementById('metersValue').value);
  const feetResult = document.getElementById('feetResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(metersValue) || metersValue < 0) {
    feetResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert meters to feet (1 meter = 3.28084 feet)
  const feetValue = metersValue * 3.28084;
  
  // Format the result
  let formattedResult;
  if (feetValue >= 1000) {
    formattedResult = feetValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (feetValue >= 1) {
    formattedResult = feetValue.toFixed(4);
  } else {
    formattedResult = feetValue.toFixed(6);
  }
  
  feetResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const metersValue = document.getElementById('metersValue').value;
  const feetResult = document.getElementById('feetResult').value;
  const copyText = `${metersValue} meters = ${feetResult} feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('metersValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('metersValue').focus();
  document.getElementById('metersValue').value = 10;
  updateConversion();
});
</script>

