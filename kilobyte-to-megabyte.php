<script>
function updateConversion() {
  const kilobyteValue = parseFloat(document.getElementById('kilobyteValue').value);
  const megabyteResult = document.getElementById('megabyteResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kilobyteValue) || kilobyteValue < 0) {
    megabyteResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert kilobytes to megabytes
  // 1 megabyte = 1,024 kilobytes
  const megabyteValue = kilobyteValue / 1024;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (megabyteValue >= 1000) {
    formattedResult = megabyteValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (megabyteValue >= 100) {
    formattedResult = megabyteValue.toFixed(2);
  } else if (megabyteValue >= 10) {
    formattedResult = megabyteValue.toFixed(3);
  } else if (megabyteValue >= 1) {
    formattedResult = megabyteValue.toFixed(4);
  } else {
    formattedResult = megabyteValue.toFixed(6);
  }
  
  megabyteResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kilobyteValue = document.getElementById('kilobyteValue').value;
  const megabyteResult = document.getElementById('megabyteResult').value;
  const copyText = `${kilobyteValue} kilobytes = ${megabyteResult} megabytes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kilobyteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kilobyteValue').focus();
  document.getElementById('kilobyteValue').value = 1024;
  updateConversion();
});
</script>

