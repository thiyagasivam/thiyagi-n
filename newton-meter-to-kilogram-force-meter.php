let isUpdating = false;

function convertNewtonMeterToKgfMeter(newtonMeter) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(newtonMeter) && newtonMeter !== '') {
        // 1 N·m = 1/9.80665 kgf·m
        const kgfMeter = newtonMeter / 9.80665;
        document.getElementById('kgfMeterInput').value = kgfMeter.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-orange-600 mr-2"></i>
            <strong>${newtonMeter.toLocaleString()} N·m = ${kgfMeter.toFixed(3)} kgf·m</strong>
        `;
    } else {
        document.getElementById('kgfMeterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertKgfMeterToNewtonMeter(kgfMeter) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(kgfMeter) && kgfMeter !== '') {
        // 1 kgf·m = 9.80665 N·m
        const newtonMeter = kgfMeter * 9.80665;
        document.getElementById('newtonMeterInput').value = newtonMeter.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-orange-600 mr-2"></i>
            <strong>${kgfMeter} kgf·m = ${newtonMeter.toFixed(3)} N·m</strong>
        `;
    } else {
        document.getElementById('newtonMeterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('newtonMeterInput').value = '';
    document.getElementById('kgfMeterInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const newtonMeterValue = document.getElementById('newtonMeterInput').value;
    const kgfMeterValue = document.getElementById('kgfMeterInput').value;
    
    document.getElementById('newtonMeterInput').value = kgfMeterValue;
    document.getElementById('kgfMeterInput').value = newtonMeterValue;
    
    if (kgfMeterValue) {
        convertNewtonMeterToKgfMeter(parseFloat(kgfMeterValue));
    } else if (newtonMeterValue) {
        convertKgfMeterToNewtonMeter(parseFloat(newtonMeterValue));
    }
}

// Add event listeners
document.getElementById('newtonMeterInput').addEventListener('input', function() {
    convertNewtonMeterToKgfMeter(parseFloat(this.value));
});

document.getElementById('kgfMeterInput').addEventListener('input', function() {
    convertKgfMeterToNewtonMeter(parseFloat(this.value));
});
</script>

