<script>
function updateConversion() {
  const mpsInput = document.getElementById('mpsValue');
  const mpsValue = parseFloat(mpsInput.value);
  const mphResult = document.getElementById('mphResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(mpsValue) || mpsValue < 0) {
    mphResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    mphResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert m/s to mph (1 m/s = 2.23694 mph)
  const mphValue = mpsValue * 2.23694;
  
  let formattedResult = mphValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 4 
  });
  
  mphResult.value = formattedResult;
  mphResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMps = mpsValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMps} m/s = ${formattedResult} mph`;
  liveConversion.classList.remove('hidden');
  
  mpsInput.classList.add('border-blue-500');
}

function copyResult() {
  const mpsValue = document.getElementById('mpsValue').value;
  const mphResult = document.getElementById('mphResult').value;
  const copyText = `${mpsValue} m/s = ${mphResult} mph`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('mpsValue').value = '';
  document.getElementById('mphResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('mphResult').classList.remove('result-highlight');
  document.getElementById('mpsValue').classList.remove('border-blue-500');
  document.getElementById('mpsValue').focus();
}

document.getElementById('mpsValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const mpsInput = document.getElementById('mpsValue');
  mpsInput.focus();
  
  setTimeout(() => {
    mpsInput.value = 10;
    updateConversion();
  }, 500);
});
</script>

