<script>
function updateConversion() {
  const fahrenheitValue = parseFloat(document.getElementById('fahrenheitValue').value);
  const celsiusResult = document.getElementById('celsiusResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(fahrenheitValue)) {
    celsiusResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert Fahrenheit to Celsius: (°F - 32) × 5/9
  const celsiusValue = (fahrenheitValue - 32) * 5/9;
  
  // Format the result
  let formattedResult;
  if (Math.abs(celsiusValue) >= 1000) {
    formattedResult = celsiusValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (Math.abs(celsiusValue) >= 100) {
    formattedResult = celsiusValue.toFixed(1);
  } else if (Math.abs(celsiusValue) >= 10) {
    formattedResult = celsiusValue.toFixed(2);
  } else {
    formattedResult = celsiusValue.toFixed(3);
  }
  
  celsiusResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const fahrenheitValue = document.getElementById('fahrenheitValue').value;
  const celsiusResult = document.getElementById('celsiusResult').value;
  const copyText = `${fahrenheitValue}°F = ${celsiusResult}°C`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('fahrenheitValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('fahrenheitValue').focus();
  document.getElementById('fahrenheitValue').value = 77;
  updateConversion();
});
</script>

