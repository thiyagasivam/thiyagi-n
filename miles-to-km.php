function updateConversion() {
  const milesValue = parseFloat(document.getElementById('milesValue').value);
  const kmResult = document.getElementById('kmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(milesValue) || milesValue < 0) {
    kmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert miles to kilometers (1 mile = 1.609344 km)
  const kmValue = milesValue * 1.609344;
  
  // Format the result
  let formattedResult;
  if (kmValue >= 1000) {
    formattedResult = kmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (kmValue >= 1) {
    formattedResult = kmValue.toFixed(4);
  } else {
    formattedResult = kmValue.toFixed(6);
  }
  
  kmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const milesValue = document.getElementById('milesValue').value;
  const kmResult = document.getElementById('kmResult').value;
  const copyText = `${milesValue} miles = ${kmResult} kilometers`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('milesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('milesValue').focus();
  document.getElementById('milesValue').value = 100;
  updateConversion();
});
</script>

