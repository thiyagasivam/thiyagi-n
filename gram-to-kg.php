<script>
function updateConversion() {
  const gramValue = parseFloat(document.getElementById('gramValue').value);
  const kgResult = document.getElementById('kgResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(gramValue) || gramValue < 0) {
    kgResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert grams to kilograms (1000 g = 1 kg)
  const kgValue = gramValue / 1000;
  
  // Format the result
  let formattedResult;
  if (kgValue >= 1000) {
    formattedResult = kgValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (kgValue >= 100) {
    formattedResult = kgValue.toFixed(2);
  } else if (kgValue >= 10) {
    formattedResult = kgValue.toFixed(3);
  } else if (kgValue >= 1) {
    formattedResult = kgValue.toFixed(4);
  } else {
    formattedResult = kgValue.toFixed(6);
  }
  
  kgResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const gramValue = document.getElementById('gramValue').value;
  const kgResult = document.getElementById('kgResult').value;
  const copyText = `${gramValue} grams = ${kgResult} kilograms`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('gramValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('gramValue').focus();
  document.getElementById('gramValue').value = 2500;
  updateConversion();
});
</script>

