function updateConversion() {
  const poundsValue = parseFloat(document.getElementById('poundsValue').value);
  const ouncesResult = document.getElementById('ouncesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(poundsValue) || poundsValue < 0) {
    ouncesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert pounds to ounces (1 pound = 16 ounces)
  const ouncesValue = poundsValue * 16;
  
  // Format the result
  let formattedResult;
  if (ouncesValue >= 1000) {
    formattedResult = ouncesValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 2 
    });
  } else if (ouncesValue >= 1) {
    formattedResult = ouncesValue.toFixed(2);
  } else {
    formattedResult = ouncesValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  ouncesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const poundsValue = document.getElementById('poundsValue').value;
  const ouncesResult = document.getElementById('ouncesResult').value;
  const copyText = `${poundsValue} pounds = ${ouncesResult} ounces`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('poundsValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('poundsValue').focus();
  document.getElementById('poundsValue').value = 2;
  updateConversion();
});
</script>

