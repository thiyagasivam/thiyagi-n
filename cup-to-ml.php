<script>
let currentUnit = 'us'; // 'us' or 'metric'

function updateConversion() {
  const cupValue = parseFloat(document.getElementById('cupValue').value);
  const mlResult = document.getElementById('mlResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(cupValue) || cupValue < 0) {
    mlResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cups to milliliters based on current unit type
  let mlValue;
  if (currentUnit === 'us') {
    // 1 US cup = 236.588 milliliters
    mlValue = cupValue * 236.588;
  } else {
    // 1 metric cup = 250 milliliters
    mlValue = cupValue * 250;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (mlValue >= 1000) {
    formattedResult = mlValue.toFixed(0);
  } else if (mlValue >= 100) {
    formattedResult = mlValue.toFixed(1);
  } else {
    formattedResult = mlValue.toFixed(2);
  }
  
  mlResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const cupValue = document.getElementById('cupValue').value;
  const mlResult = document.getElementById('mlResult').value;
  const unitType = currentUnit === 'us' ? 'US cups' : 'metric cups';
  const copyText = `${cupValue} ${unitType} = ${mlResult} milliliters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usCupBtn');
  const metricBtn = document.getElementById('metricCupBtn');
  const cupLabel = document.getElementById('cupLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    cupLabel.textContent = 'US Cups';
  } else {
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    cupLabel.textContent = 'Metric Cups';
  }
  
  updateConversion();
}

document.getElementById('cupValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usCupBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('metricCupBtn').addEventListener('click', () => switchUnit('metric'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('cupValue').focus();
  document.getElementById('cupValue').value = 2;
  updateConversion();
});
</script>

