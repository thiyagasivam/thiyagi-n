let isUpdating = false;

function convertPoundForceInchToNewtonMeter(poundForceInches) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(poundForceInches) && poundForceInches !== '') {
        const newtonMeters = poundForceInches * 0.1129848290276167;
        document.getElementById('newtonMeterInput').value = newtonMeters.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-red-600 mr-2"></i>
            <strong>${poundForceInches} lbf⋅in = ${newtonMeters.toFixed(6)} N⋅m</strong>
        `;
    } else {
        document.getElementById('newtonMeterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertNewtonMeterToPoundForceInch(newtonMeters) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(newtonMeters) && newtonMeters !== '') {
        const poundForceInches = newtonMeters * 8.8507461068334;
        document.getElementById('poundForceInchInput').value = poundForceInches.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-red-600 mr-2"></i>
            <strong>${newtonMeters} N⋅m = ${poundForceInches.toFixed(6)} lbf⋅in</strong>
        `;
    } else {
        document.getElementById('poundForceInchInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('poundForceInchInput').value = '';
    document.getElementById('newtonMeterInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const poundForceInchValue = document.getElementById('poundForceInchInput').value;
    const newtonMeterValue = document.getElementById('newtonMeterInput').value;
    
    document.getElementById('poundForceInchInput').value = newtonMeterValue;
    document.getElementById('newtonMeterInput').value = poundForceInchValue;
    
    if (newtonMeterValue) {
        convertPoundForceInchToNewtonMeter(parseFloat(newtonMeterValue));
    } else if (poundForceInchValue) {
        convertNewtonMeterToPoundForceInch(parseFloat(poundForceInchValue));
    }
}

// Add event listeners
document.getElementById('poundForceInchInput').addEventListener('input', function() {
    convertPoundForceInchToNewtonMeter(parseFloat(this.value));
});

document.getElementById('newtonMeterInput').addEventListener('input', function() {
    convertNewtonMeterToPoundForceInch(parseFloat(this.value));
});
</script>

