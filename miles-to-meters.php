<script>
function updateConversion() {
  const milesInput = document.getElementById('milesValue');
  const milesValue = parseFloat(milesInput.value);
  const metersResult = document.getElementById('metersResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(milesValue) || milesValue < 0) {
    metersResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    metersResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert miles to meters (1 mile = 1609.34 meters)
  const metersValue = milesValue * 1609.34;
  
  let formattedResult = metersValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 2 
  });
  
  metersResult.value = formattedResult;
  metersResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMiles = milesValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMiles} mi = ${formattedResult} m`;
  liveConversion.classList.remove('hidden');
  
  milesInput.classList.add('border-gray-500');
}

function copyResult() {
  const milesValue = document.getElementById('milesValue').value;
  const metersResult = document.getElementById('metersResult').value;
  const copyText = `${milesValue} miles = ${metersResult} meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('milesValue').value = '';
  document.getElementById('metersResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('metersResult').classList.remove('result-highlight');
  document.getElementById('milesValue').classList.remove('border-gray-500');
  document.getElementById('milesValue').focus();
}

document.getElementById('milesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const milesInput = document.getElementById('milesValue');
  milesInput.focus();
  
  setTimeout(() => {
    milesInput.value = 1;
    updateConversion();
  }, 500);
});
</script>

