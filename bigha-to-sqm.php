<script>
function updateConversion() {
  const bighaValue = parseFloat(document.getElementById('bighaValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(bighaValue) || bighaValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert bigha to square meters (1 bigha = 2529 square meters - standard conversion)
  const sqmValue = bighaValue * 2529;
  
  // Format the result
  let formattedResult;
  if (sqmValue >= 100000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqmValue >= 1000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else {
    formattedResult = sqmValue.toFixed(2);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const bighaValue = document.getElementById('bighaValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${bighaValue} bigha = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('bighaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('bighaValue').focus();
  document.getElementById('bighaValue').value = 3;
  updateConversion();
});
</script>

