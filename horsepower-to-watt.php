<script>
function updateConversion() {
  const horsepowerValue = parseFloat(document.getElementById('horsepowerValue').value);
  const wattResult = document.getElementById('wattResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(horsepowerValue) || horsepowerValue < 0) {
    wattResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert horsepower to watts
  // 1 horsepower = 745.7 watts (mechanical horsepower)
  const wattValue = horsepowerValue * 745.7;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (wattValue >= 1000000) {
    formattedResult = wattValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (wattValue >= 10000) {
    formattedResult = wattValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (wattValue >= 1000) {
    formattedResult = wattValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (wattValue >= 100) {
    formattedResult = wattValue.toFixed(1);
  } else if (wattValue >= 10) {
    formattedResult = wattValue.toFixed(2);
  } else {
    formattedResult = wattValue.toFixed(3);
  }
  
  wattResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const horsepowerValue = document.getElementById('horsepowerValue').value;
  const wattResult = document.getElementById('wattResult').value;
  const copyText = `${horsepowerValue} horsepower = ${wattResult} watts`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('horsepowerValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('horsepowerValue').focus();
  document.getElementById('horsepowerValue').value = 1;
  updateConversion();
});
</script>

