<script>
let currentUnit = 'us'; // 'us' or 'uk'

function updateConversion() {
  const literValue = parseFloat(document.getElementById('literValue').value);
  const gallonResult = document.getElementById('gallonResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(literValue) || literValue < 0) {
    gallonResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert liters to gallons based on current unit type
  let gallonValue;
  if (currentUnit === 'us') {
    // 1 liter = 0.264172 US gallons
    gallonValue = literValue * 0.264172;
  } else {
    // 1 liter = 0.219969 UK gallons
    gallonValue = literValue * 0.219969;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (gallonValue >= 100) {
    formattedResult = gallonValue.toFixed(1);
  } else if (gallonValue >= 10) {
    formattedResult = gallonValue.toFixed(2);
  } else {
    formattedResult = gallonValue.toFixed(3);
  }
  
  gallonResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const literValue = document.getElementById('literValue').value;
  const gallonResult = document.getElementById('gallonResult').value;
  const unitType = currentUnit === 'us' ? 'US gallons' : 'UK gallons';
  const copyText = `${literValue} liters = ${gallonResult} ${unitType}`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usGallonBtn');
  const ukBtn = document.getElementById('ukGallonBtn');
  const gallonLabel = document.getElementById('gallonLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    gallonLabel.textContent = 'US Gallons';
  } else {
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    gallonLabel.textContent = 'UK Gallons';
  }
  
  updateConversion();
}

document.getElementById('literValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usGallonBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('ukGallonBtn').addEventListener('click', () => switchUnit('uk'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('literValue').focus();
  document.getElementById('literValue').value = 20;
  updateConversion();
});
</script>

