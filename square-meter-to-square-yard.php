<script>
function updateConversion() {
  const sqmValue = parseFloat(document.getElementById('sqmValue').value);
  const sqyardResult = document.getElementById('sqyardResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqmValue) || sqmValue < 0) {
    sqyardResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square meters to square yards
  // 1 square meter = 1.19599 square yards
  const sqyardValue = sqmValue * 1.19599;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqyardValue >= 100) {
    formattedResult = sqyardValue.toFixed(1);
  } else if (sqyardValue >= 10) {
    formattedResult = sqyardValue.toFixed(2);
  } else if (sqyardValue >= 1) {
    formattedResult = sqyardValue.toFixed(3);
  } else {
    formattedResult = sqyardValue.toFixed(4);
  }
  
  sqyardResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqmValue = document.getElementById('sqmValue').value;
  const sqyardResult = document.getElementById('sqyardResult').value;
  const copyText = `${sqmValue} square meters = ${sqyardResult} square yards`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqmValue').focus();
  document.getElementById('sqmValue').value = 10;
  updateConversion();
});
</script>

