<script>
function updateConversion() {
  const cubicFeetInput = document.getElementById('cubicFeetValue');
  const cubicFeetValue = parseFloat(cubicFeetInput.value);
  const cubicYardsResult = document.getElementById('cubicYardsResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(cubicFeetValue) || cubicFeetValue < 0) {
    cubicYardsResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    cubicYardsResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert cubic feet to cubic yards (1 cubic yard = 27 cubic feet)
  const cubicYardsValue = cubicFeetValue / 27;
  
  let formattedResult = cubicYardsValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  cubicYardsResult.value = formattedResult;
  cubicYardsResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedCubicFeet = cubicFeetValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedCubicFeet} ft³ = ${formattedResult} yd³`;
  liveConversion.classList.remove('hidden');
  
  cubicFeetInput.classList.add('border-purple-500');
}

function copyResult() {
  const cubicFeetValue = document.getElementById('cubicFeetValue').value;
  const cubicYardsResult = document.getElementById('cubicYardsResult').value;
  const copyText = `${cubicFeetValue} ft³ = ${cubicYardsResult} yd³`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('cubicFeetValue').value = '';
  document.getElementById('cubicYardsResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('cubicYardsResult').classList.remove('result-highlight');
  document.getElementById('cubicFeetValue').classList.remove('border-purple-500');
  document.getElementById('cubicFeetValue').focus();
}

document.getElementById('cubicFeetValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const cubicFeetInput = document.getElementById('cubicFeetValue');
  cubicFeetInput.focus();
  
  setTimeout(() => {
    cubicFeetInput.value = 54;
    updateConversion();
  }, 500);
});
</script>

