<script>
let currentUnit = 'us'; // 'us' or 'uk'

function updateConversion() {
  const pintValue = parseFloat(document.getElementById('pintValue').value);
  const literResult = document.getElementById('literResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(pintValue) || pintValue < 0) {
    literResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert pints to liters based on current unit type
  let literValue;
  if (currentUnit === 'us') {
    // 1 US pint = 0.473176 liters
    literValue = pintValue * 0.473176;
  } else {
    // 1 UK pint = 0.568261 liters
    literValue = pintValue * 0.568261;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (literValue >= 10) {
    formattedResult = literValue.toFixed(2);
  } else if (literValue >= 1) {
    formattedResult = literValue.toFixed(3);
  } else {
    formattedResult = literValue.toFixed(4);
  }
  
  literResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const pintValue = document.getElementById('pintValue').value;
  const literResult = document.getElementById('literResult').value;
  const unitType = currentUnit === 'us' ? 'US pints' : 'UK pints';
  const copyText = `${pintValue} ${unitType} = ${literResult} liters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usPintBtn');
  const ukBtn = document.getElementById('ukPintBtn');
  const pintLabel = document.getElementById('pintLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    pintLabel.textContent = 'US Pints';
  } else {
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    pintLabel.textContent = 'UK Pints';
  }
  
  updateConversion();
}

document.getElementById('pintValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usPintBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('ukPintBtn').addEventListener('click', () => switchUnit('uk'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('pintValue').focus();
  document.getElementById('pintValue').value = 2;
  updateConversion();
});
</script>

