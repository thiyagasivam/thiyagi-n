<script>
function updateConversion() {
  const ounceValue = parseFloat(document.getElementById('ounceValue').value);
  const gramResult = document.getElementById('gramResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(ounceValue) || ounceValue < 0) {
    gramResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert ounces to grams (1 oz = 28.3495 g)
  const gramValue = ounceValue * 28.3495;
  
  // Format the result
  let formattedResult;
  if (gramValue >= 10000) {
    formattedResult = gramValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (gramValue >= 1000) {
    formattedResult = gramValue.toFixed(2);
  } else if (gramValue >= 100) {
    formattedResult = gramValue.toFixed(3);
  } else if (gramValue >= 10) {
    formattedResult = gramValue.toFixed(4);
  } else {
    formattedResult = gramValue.toFixed(5);
  }
  
  gramResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const ounceValue = document.getElementById('ounceValue').value;
  const gramResult = document.getElementById('gramResult').value;
  const copyText = `${ounceValue} ounces = ${gramResult} grams`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('ounceValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('ounceValue').focus();
  document.getElementById('ounceValue').value = 8;
  updateConversion();
});
</script>

