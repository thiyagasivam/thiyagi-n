<script>
let isUpdating = false;

function convertMlToFlOzUK(milliliters) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(milliliters) && milliliters !== '') {
        const fluidOunces = milliliters / 28.413062;
        document.getElementById('fluidOunceInput').value = fluidOunces.toFixed(10);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-indigo-600 mr-2"></i>
            <strong>${milliliters} mL = ${fluidOunces.toFixed(6)} fl oz UK</strong>
        `;
    } else {
        document.getElementById('fluidOunceInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertFlOzUKToMl(fluidOunces) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(fluidOunces) && fluidOunces !== '') {
        const milliliters = fluidOunces * 28.413062;
        document.getElementById('milliliterInput').value = milliliters.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-indigo-600 mr-2"></i>
            <strong>${fluidOunces} fl oz UK = ${milliliters.toFixed(3)} mL</strong>
        `;
    } else {
        document.getElementById('milliliterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('milliliterInput').value = '';
    document.getElementById('fluidOunceInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const mlValue = document.getElementById('milliliterInput').value;
    const flOzValue = document.getElementById('fluidOunceInput').value;
    
    document.getElementById('milliliterInput').value = flOzValue;
    document.getElementById('fluidOunceInput').value = mlValue;
    
    if (flOzValue) {
        convertMlToFlOzUK(parseFloat(flOzValue));
    } else if (mlValue) {
        convertFlOzUKToMl(parseFloat(mlValue));
    }
}

// Add event listeners
document.getElementById('milliliterInput').addEventListener('input', function() {
    convertMlToFlOzUK(parseFloat(this.value));
});

document.getElementById('fluidOunceInput').addEventListener('input', function() {
    convertFlOzUKToMl(parseFloat(this.value));
});
</script>

