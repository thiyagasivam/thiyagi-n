<script>
function updateConversion() {
  const kilowattValue = parseFloat(document.getElementById('kilowattValue').value);
  const horsepowerResult = document.getElementById('horsepowerResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kilowattValue) || kilowattValue < 0) {
    horsepowerResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert kilowatts to horsepower
  // 1 kilowatt = 1.341 horsepower (mechanical horsepower)
  const horsepowerValue = kilowattValue * 1.341;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (horsepowerValue >= 1000) {
    formattedResult = horsepowerValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (horsepowerValue >= 100) {
    formattedResult = horsepowerValue.toFixed(2);
  } else if (horsepowerValue >= 10) {
    formattedResult = horsepowerValue.toFixed(3);
  } else if (horsepowerValue >= 1) {
    formattedResult = horsepowerValue.toFixed(4);
  } else {
    formattedResult = horsepowerValue.toFixed(5);
  }
  
  horsepowerResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kilowattValue = document.getElementById('kilowattValue').value;
  const horsepowerResult = document.getElementById('horsepowerResult').value;
  const copyText = `${kilowattValue} kilowatts = ${horsepowerResult} horsepower`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kilowattValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kilowattValue').focus();
  document.getElementById('kilowattValue').value = 1;
  updateConversion();
});
</script>

