  }
  const res = convertArea(val, from);
  let html = '';
  for (const k of Object.keys(unitLabels)) {
    if (k === from) continue;
    html += `<div class='bg-blue-50 rounded-lg p-4 flex flex-col gap-1'><span class='text-gray-700 font-medium'>${unitLabels[k]}</span><span class='text-xl font-bold text-blue-700'>${formatNum(res[k])}</span></div>`;
  }
  resultsDiv.innerHTML = html;
  
  // Show/hide copy button based on whether there are results
  const copyBtn = document.getElementById('copyBtn');
  if (html.trim()) {
    copyBtn.classList.remove('hidden');
  } else {
    copyBtn.classList.add('hidden');
  }
}
