<script>
function updateConversion() {
  const inchesValue = parseFloat(document.getElementById('inchesValue').value);
  const cmResult = document.getElementById('cmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(inchesValue) || inchesValue < 0) {
    cmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert inches to centimeters (1 inch = 2.54 centimeters)
  const cmValue = inchesValue * 2.54;
  
  // Format the result
  let formattedResult;
  if (cmValue >= 100) {
    formattedResult = cmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (cmValue >= 1) {
    formattedResult = cmValue.toFixed(3);
  } else {
    formattedResult = cmValue.toFixed(4);
  }
  
  cmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const inchesValue = document.getElementById('inchesValue').value;
  const cmResult = document.getElementById('cmResult').value;
  const copyText = `${inchesValue} inches = ${cmResult} centimeters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('inchesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('inchesValue').focus();
  document.getElementById('inchesValue').value = 6;
  updateConversion();
});
</script>

