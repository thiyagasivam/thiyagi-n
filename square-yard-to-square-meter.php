<script>
function updateConversion() {
  const sqyardValue = parseFloat(document.getElementById('sqyardValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqyardValue) || sqyardValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square yards to square meters
  // 1 square yard = 0.83613 square meters
  const sqmValue = sqyardValue * 0.83613;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (sqmValue >= 100) {
    formattedResult = sqmValue.toFixed(1);
  } else if (sqmValue >= 10) {
    formattedResult = sqmValue.toFixed(2);
  } else if (sqmValue >= 1) {
    formattedResult = sqmValue.toFixed(3);
  } else {
    formattedResult = sqmValue.toFixed(4);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqyardValue = document.getElementById('sqyardValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${sqyardValue} square yards = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqyardValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqyardValue').focus();
  document.getElementById('sqyardValue').value = 10;
  updateConversion();
});
</script>

