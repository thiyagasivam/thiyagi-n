<script>
function updateConversion() {
  const kilowattInput = document.getElementById('kilowattValue');
  const kilowattValue = parseFloat(kilowattInput.value);
  const horsepowerResult = document.getElementById('horsepowerResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(kilowattValue) || kilowattValue < 0) {
    horsepowerResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    horsepowerResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert kilowatts to horsepower (1 kW = 1.34102 HP - mechanical horsepower)
  const horsepowerValue = kilowattValue * 1.34102;
  
  let formattedResult = horsepowerValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 5 
  });
  
  horsepowerResult.value = formattedResult;
  horsepowerResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedKilowatt = kilowattValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedKilowatt} kW = ${formattedResult} HP`;
  liveConversion.classList.remove('hidden');
  
  kilowattInput.classList.add('border-red-500');
}

function copyResult() {
  const kilowattValue = document.getElementById('kilowattValue').value;
  const horsepowerResult = document.getElementById('horsepowerResult').value;
  const copyText = `${kilowattValue} kW = ${horsepowerResult} HP`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('kilowattValue').value = '';
  document.getElementById('horsepowerResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('horsepowerResult').classList.remove('result-highlight');
  document.getElementById('kilowattValue').classList.remove('border-red-500');
  document.getElementById('kilowattValue').focus();
}

document.getElementById('kilowattValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const kilowattInput = document.getElementById('kilowattValue');
  kilowattInput.focus();
  
  setTimeout(() => {
    kilowattInput.value = 100;
    updateConversion();
  }, 500);
});
</script>

