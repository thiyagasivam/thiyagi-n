<script>
function updateConversion() {
  const stoneValue = parseFloat(document.getElementById('stoneValue').value);
  const poundResult = document.getElementById('poundResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(stoneValue) || stoneValue < 0) {
    poundResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert stones to pounds
  // 1 stone = 14 pounds
  const poundValue = stoneValue * 14;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (poundValue >= 1000) {
    formattedResult = poundValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (poundValue >= 100) {
    formattedResult = poundValue.toFixed(1);
  } else if (poundValue >= 10) {
    formattedResult = poundValue.toFixed(2);
  } else {
    formattedResult = poundValue.toFixed(3);
  }
  
  poundResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const stoneValue = document.getElementById('stoneValue').value;
  const poundResult = document.getElementById('poundResult').value;
  const copyText = `${stoneValue} stones = ${poundResult} pounds`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('stoneValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('stoneValue').focus();
  document.getElementById('stoneValue').value = 10;
  updateConversion();
});
</script>

