// Enhanced conversion function with advanced features
function updateConversion() {
  const gramInput = document.getElementById('gramValue');
  const gramValue = parseFloat(gramInput.value);
  const kgResult = document.getElementById('kgResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(gramValue) || gramValue < 0) {
    kgResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    kgResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert grams to kilograms (1000 g = 1 kg)
  const kgValue = gramValue / 1000;
  
  // Advanced formatting based on value size
  let formattedResult;
  if (kgValue >= 1000000) {
    formattedResult = kgValue.toExponential(3);
  } else if (kgValue >= 1000) {
    formattedResult = kgValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 2 
    });
  } else if (kgValue >= 100) {
    formattedResult = kgValue.toFixed(3);
  } else if (kgValue >= 10) {
    formattedResult = kgValue.toFixed(4);
  } else if (kgValue >= 1) {
    formattedResult = kgValue.toFixed(5);
  } else {
    formattedResult = kgValue.toFixed(6);
  }
  
  // Update result field
  kgResult.value = formattedResult;
  kgResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedGrams = gramValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedGrams} g = ${formattedResult} kg`;
  liveConversion.classList.remove('hidden');
  
  // Add visual feedback
  gramInput.classList.add('border-emerald-500');
}

// Enhanced copy function
function copyResult() {
  const gramValue = document.getElementById('gramValue').value;
  const kgResult = document.getElementById('kgResult').value;
  const copyText = `${gramValue} grams = ${kgResult} kilograms`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('gramValue').value = '';
  document.getElementById('kgResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('kgResult').classList.remove('result-highlight');
  document.getElementById('gramValue').classList.remove('border-emerald-500');
  document.getElementById('gramValue').focus();
}

// Event listeners
document.getElementById('gramValue').addEventListener('input', updateConversion);
document.getElementById('gramValue').addEventListener('keypress', function(e) {
  if (e.key === 'Enter') {
    this.blur();
    if (this.value) {
      document.getElementById('copyBtn').focus();
    }
  }
});

document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Keyboard shortcuts
document.addEventListener('keydown', function(e) {
  if (e.ctrlKey && e.key === 'c' && !document.getElementById('copyBtn').classList.contains('hidden')) {
    e.preventDefault();
    copyResult();
  }
  if (e.key === 'Escape') {
    clearAll();
  }
});

