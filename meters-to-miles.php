function updateConversion() {
  const metersValue = parseFloat(document.getElementById('metersValue').value);
  const milesResult = document.getElementById('milesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(metersValue) || metersValue < 0) {
    milesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert meters to miles (1 meter = 0.000621371192 miles)
  const milesValue = metersValue * 0.000621371192;
  
  // Format the result
  let formattedResult;
  if (milesValue >= 1) {
    formattedResult = milesValue.toFixed(3);
  } else if (milesValue >= 0.001) {
    formattedResult = milesValue.toFixed(6);
  } else {
    formattedResult = milesValue.toExponential(3);
  }
  
  // Remove trailing zeros
  if (milesValue >= 0.001) {
    formattedResult = parseFloat(formattedResult).toString();
  }
  
  milesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const metersValue = document.getElementById('metersValue').value;
  const milesResult = document.getElementById('milesResult').value;
  const copyText = `${metersValue} meters = ${milesResult} miles`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('metersValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('metersValue').focus();
  document.getElementById('metersValue').value = 1000;
  updateConversion();
});
</script>

