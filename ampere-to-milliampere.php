<script>
function updateConversion() {
  const ampereValue = parseFloat(document.getElementById('ampereValue').value);
  const milliampereResult = document.getElementById('milliampereResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(ampereValue) || ampereValue < 0) {
    milliampereResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert amperes to milliamperes
  // 1 ampere = 1,000 milliamperes
  const milliampereValue = ampereValue * 1000;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (milliampereValue >= 1000000) {
    formattedResult = milliampereValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (milliampereValue >= 1000) {
    formattedResult = milliampereValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (milliampereValue >= 100) {
    formattedResult = milliampereValue.toFixed(1);
  } else if (milliampereValue >= 10) {
    formattedResult = milliampereValue.toFixed(2);
  } else if (milliampereValue >= 1) {
    formattedResult = milliampereValue.toFixed(3);
  } else {
    formattedResult = milliampereValue.toFixed(4);
  }
  
  milliampereResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const ampereValue = document.getElementById('ampereValue').value;
  const milliampereResult = document.getElementById('milliampereResult').value;
  const copyText = `${ampereValue} amperes = ${milliampereResult} milliamperes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('ampereValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('ampereValue').focus();
  document.getElementById('ampereValue').value = 1;
  updateConversion();
});
