// Enhanced conversion function
function updateConversion() {
  const yardInput = document.getElementById('yardValue');
  const yardValue = parseFloat(yardInput.value);
  const feetResult = document.getElementById('feetResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(yardValue) || yardValue < 0) {
    feetResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    feetResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert yards to feet (1 yard = 3 feet)
  const feetValue = yardValue * 3;
  
  // Format result
  let formattedResult = feetValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  // Update result field
  feetResult.value = formattedResult;
  feetResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedYards = yardValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedYards} yd = ${formattedResult} ft`;
  liveConversion.classList.remove('hidden');
  
  yardInput.classList.add('border-red-500');
}

// Copy function
function copyResult() {
  const yardValue = document.getElementById('yardValue').value;
  const feetResult = document.getElementById('feetResult').value;
  const copyText = `${yardValue} yards = ${feetResult} feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('yardValue').value = '';
  document.getElementById('feetResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('feetResult').classList.remove('result-highlight');
  document.getElementById('yardValue').classList.remove('border-red-500');
  document.getElementById('yardValue').focus();
}

// Event listeners
document.getElementById('yardValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Auto-focus and demo value on page load
window.addEventListener('DOMContentLoaded', () => {
  const yardInput = document.getElementById('yardValue');
  yardInput.focus();
  
  setTimeout(() => {
    yardInput.value = 100;
    updateConversion();
  }, 500);
});
</script>

