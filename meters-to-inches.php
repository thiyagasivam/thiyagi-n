function updateConversion() {
  const metersValue = parseFloat(document.getElementById('metersValue').value);
  const inchesResult = document.getElementById('inchesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(metersValue) || metersValue < 0) {
    inchesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert meters to inches (1 meter = 39.3700787402 inches)
  const inchesValue = metersValue * 39.3700787402;
  
  // Format the result
  let formattedResult;
  if (inchesValue >= 1000) {
    formattedResult = inchesValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (inchesValue >= 1) {
    formattedResult = inchesValue.toFixed(2);
  } else {
    formattedResult = inchesValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  inchesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const metersValue = document.getElementById('metersValue').value;
  const inchesResult = document.getElementById('inchesResult').value;
  const copyText = `${metersValue} meters = ${inchesResult} inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('metersValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('metersValue').focus();
  document.getElementById('metersValue').value = 1;
  updateConversion();
});
</script>

