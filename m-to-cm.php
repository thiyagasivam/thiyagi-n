<script>
function updateConversion() {
  const meterInput = document.getElementById('meterValue');
  const meterValue = parseFloat(meterInput.value);
  const cmResult = document.getElementById('cmResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(meterValue) || meterValue < 0) {
    cmResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    cmResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert meters to centimeters (1 m = 100 cm)
  const cmValue = meterValue * 100;
  
  let formattedResult = cmValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  cmResult.value = formattedResult;
  cmResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedMeters = meterValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedMeters} m = ${formattedResult} cm`;
  liveConversion.classList.remove('hidden');
  
  meterInput.classList.add('border-emerald-500');
}

function copyResult() {
  const meterValue = document.getElementById('meterValue').value;
  const cmResult = document.getElementById('cmResult').value;
  const copyText = `${meterValue} m = ${cmResult} cm`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('meterValue').value = '';
  document.getElementById('cmResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('cmResult').classList.remove('result-highlight');
  document.getElementById('meterValue').classList.remove('border-emerald-500');
  document.getElementById('meterValue').focus();
}

document.getElementById('meterValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const meterInput = document.getElementById('meterValue');
  meterInput.focus();
  
  setTimeout(() => {
    meterInput.value = 1.5;
    updateConversion();
  }, 500);
});
</script>

