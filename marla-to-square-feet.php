<script>
function updateConversion() {
  const marlaValue = parseFloat(document.getElementById('marlaValue').value);
  const sqFtResult = document.getElementById('sqFtResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(marlaValue) || marlaValue < 0) {
    sqFtResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert marla to square feet (1 marla = 272.25 square feet)
  const sqFtValue = marlaValue * 272.25;
  
  // Format the result with appropriate decimal places and thousands separators
  let formattedResult;
  if (sqFtValue >= 10000) {
    formattedResult = sqFtValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (sqFtValue >= 1000) {
    formattedResult = sqFtValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else {
    formattedResult = sqFtValue.toFixed(2);
  }
  
  sqFtResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const marlaValue = document.getElementById('marlaValue').value;
  const sqFtResult = document.getElementById('sqFtResult').value;
  const copyText = `${marlaValue} marla = ${sqFtResult} square feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('marlaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('marlaValue').focus();
  document.getElementById('marlaValue').value = 5;
  updateConversion();
});
</script>

