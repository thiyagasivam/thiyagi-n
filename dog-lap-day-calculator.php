                const age = parseInt(document.getElementById('dogAge').value);
                const size = document.getElementById('dogSize').value;
                const breedType = document.getElementById('breedType').value;
                const energyLevel = document.getElementById('energyLevel').value;
                const cuddliness = document.getElementById('cuddliness').value;
                const ownerSchedule = document.getElementById('ownerSchedule').value;
                const lapPreference = document.getElementById('lapPreference').value;

                if (!size || !breedType || !energyLevel || !cuddliness || !ownerSchedule || !lapPreference) {
                    alert('Please fill in all the required fields to calculate your dog\'s lap schedule!');
                    return false;
                }

                this.dogProfile = {
                    name: name || 'Your Dog',
                    age: age || 3,
                    size,
                    breedType,
                    energyLevel,
                    cuddliness,
                    ownerSchedule,
                    lapPreference,
                    puppyStage: document.getElementById('puppyStage').checked,
                    seniorDog: document.getElementById('seniorDog').checked,
                    anxiousDog: document.getElementById('anxiousDog').checked,
                    newAdoption: document.getElementById('newAdoption').checked
                };

                return true;
            }

            generateLapSchedule() {
                let lapDaysPerWeek = this.calculateLapDays();
                let sessionDuration = this.calculateSessionDuration();
                let bestTime = this.calculateBestTime();
                
                // Apply special modifiers
                if (this.dogProfile.puppyStage) lapDaysPerWeek = Math.min(7, lapDaysPerWeek + 1);
                if (this.dogProfile.seniorDog) sessionDuration += 15;
                if (this.dogProfile.anxiousDog) lapDaysPerWeek = Math.min(7, lapDaysPerWeek + 2);
                if (this.dogProfile.newAdoption) lapDaysPerWeek = Math.min(7, lapDaysPerWeek + 1);

                return {
                    lapDaysPerWeek: Math.max(1, Math.min(7, lapDaysPerWeek)),
                    sessionDuration: Math.max(10, Math.min(120, sessionDuration)),
                    bestTime,
                    weeklySchedule: this.generateWeeklySchedule(lapDaysPerWeek),
                    compatibility: this.calculateCompatibility(),
                    tips: this.generateTips(),
                    funStats: this.generateFunStats(lapDaysPerWeek, sessionDuration)
                };
            }

            calculateLapDays() {
                let baseDays = 3;

                // Size factor
                const sizeFactors = {
                    tiny: 5, small: 4, medium: 3, large: 2, giant: 1
                };
                baseDays = sizeFactors[this.dogProfile.size] || 3;

                // Breed factor
                const breedFactors = {
                    lap: 2, companion: 1, working: -1, sporting: 0, terrier: 0, hound: -1, mixed: 0
                };
                baseDays += breedFactors[this.dogProfile.breedType] || 0;

                // Cuddliness factor
                const cuddlinessFactors = {
                    independent: -2, selective: -1, affectionate: 1, velcro: 2
                };
                baseDays += cuddlinessFactors[this.dogProfile.cuddliness] || 0;

                // Owner preference factor
                const preferenceFactors = {
                    love: 2, enjoy: 1, tolerate: 0, limited: -2
                };
                baseDays += preferenceFactors[this.dogProfile.lapPreference] || 0;

                // Owner schedule factor
                const scheduleFactors = {
                    'very-busy': -1, busy: 0, moderate: 1, flexible: 2
                };
                baseDays += scheduleFactors[this.dogProfile.ownerSchedule] || 0;

                return Math.max(1, Math.min(7, baseDays));
            }

            calculateSessionDuration() {
                let baseDuration = 30;

                // Size factor - smaller dogs can handle longer sessions
                const sizeDurations = {
                    tiny: 45, small: 40, medium: 30, large: 25, giant: 20
                };
                baseDuration = sizeDurations[this.dogProfile.size] || 30;

                // Energy level factor
                const energyFactors = {
                    low: 20, moderate: 0, high: -10, extreme: -15
                };
                baseDuration += energyFactors[this.dogProfile.energyLevel] || 0;

                // Age factor
                if (this.dogProfile.age < 1) baseDuration += 10; // Puppies need more comfort
                if (this.dogProfile.age > 8) baseDuration += 15; // Seniors appreciate longer cuddles

                return Math.max(10, Math.min(120, baseDuration));
            }

            calculateBestTime() {
                const energyLevel = this.dogProfile.energyLevel;
                const ownerSchedule = this.dogProfile.ownerSchedule;

                if (energyLevel === 'low' || this.dogProfile.seniorDog) return 'Morning';
                if (energyLevel === 'extreme') return 'After Exercise';
                if (ownerSchedule === 'very-busy' || ownerSchedule === 'busy') return 'Evening';
                
                return 'Evening';
            }

            generateWeeklySchedule(lapDays) {
                const days = ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'];
                const schedule = [];

                // Distribute lap days evenly throughout the week
                const interval = Math.floor(7 / lapDays);
                let nextLapDay = 0;

                for (let i = 0; i < 7; i++) {
                    const isLapDay = i === nextLapDay && lapDays > 0;
                    schedule.push({
                        day: days[i],
                        isLapDay: isLapDay,
                        activity: isLapDay ? 'Lap Time! ðŸ•' : 'Regular Play'
                    });

                    if (isLapDay) {
                        lapDays--;
                        nextLapDay = Math.min(6, nextLapDay + interval + 1);
                    }
                }

                return schedule;
            }

            calculateCompatibility() {
                let score = 50; // Base compatibility

                // Size compatibility
                if (this.dogProfile.size === 'tiny' || this.dogProfile.size === 'small') score += 20;
                if (this.dogProfile.size === 'giant') score -= 10;

                // Cuddliness compatibility
                const cuddlinessScores = {
                    independent: -20, selective: -5, affectionate: 15, velcro: 25
                };
                score += cuddlinessScores[this.dogProfile.cuddliness] || 0;

                // Owner preference compatibility
                const preferenceScores = {
                    love: 20, enjoy: 10, tolerate: -5, limited: -15
                };
                score += preferenceScores[this.dogProfile.lapPreference] || 0;

                // Breed compatibility
                if (this.dogProfile.breedType === 'lap') score += 15;
                if (this.dogProfile.breedType === 'companion') score += 10;

                return Math.max(0, Math.min(100, score));
            }

            generateTips() {
                const tips = [];
                
                if (this.dogProfile.size === 'large' || this.dogProfile.size === 'giant') {
                    tips.push({
                        icon: 'fas fa-couch',
                        text: 'Consider floor cuddles or sharing the couch instead of traditional lap sitting.'
                    });
                }

                if (this.dogProfile.energyLevel === 'high' || this.dogProfile.energyLevel === 'extreme') {
                    tips.push({
                        icon: 'fas fa-running',
                        text: 'Schedule lap time after exercise when your dog is more likely to relax.'
                    });
                }

                if (this.dogProfile.cuddliness === 'independent') {
                    tips.push({
                        icon: 'fas fa-user-friends',
                        text: 'Respect personal space - try sitting nearby rather than forcing cuddles.'
                    });
                }

                if (this.dogProfile.anxiousDog) {
                    tips.push({
                        icon: 'fas fa-heart-broken',
                        text: 'Extra lap time can help reduce anxiety and build confidence.'
                    });
                }

                if (this.dogProfile.puppyStage) {
                    tips.push({
                        icon: 'fas fa-baby',
                        text: 'Puppies need lots of comfort and bonding time for proper development.'
                    });
                }

                // Add default tips if none specific
                if (tips.length === 0) {
                    tips.push(
                        {
                            icon: 'fas fa-clock',
                            text: 'Start with shorter sessions and gradually increase based on comfort.'
                        },
                        {
                            icon: 'fas fa-thermometer-half',
                            text: 'Lap time is especially important during cold weather for warmth.'
                        }
                    );
                }

                return tips;
            }

            generateFunStats(lapDays, duration) {
                const weeklyMinutes = lapDays * duration;
                const yearlyHours = Math.round((weeklyMinutes * 52) / 60);
                const yearlyTreats = lapDays * 52 * 2; // Assume 2 treats per lap session

                return [
                    {
                        value: weeklyMinutes,
                        label: 'Minutes Per Week',
                        icon: 'fas fa-clock',
                        color: 'text-blue-600'
                    },
                    {
                        value: yearlyHours,
                        label: 'Hours Per Year',
                        icon: 'fas fa-calendar-alt',
                        color: 'text-green-600'
                    },
                    {
                        value: yearlyTreats,
                        label: 'Treats Earned',
                        icon: 'fas fa-bone',
                        color: 'text-orange-600'
                    }
                ];
            }

