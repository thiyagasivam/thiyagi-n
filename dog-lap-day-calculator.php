            }

            updateMatchAnalysis() {
                const compatibility = this.lapSchedule.compatibility;
                let grade, message, color;

                if (compatibility >= 80) {
                    grade = 'Perfect Match!';
                    message = 'You and your dog are made for lap time!';
                    color = 'text-green-600';
                } else if (compatibility >= 60) {
                    grade = 'Great Compatibility';
                    message = 'Lap time should work well for both of you.';
                    color = 'text-blue-600';
                } else if (compatibility >= 40) {
                    grade = 'Moderate Match';
                    message = 'Some adjustment may be needed for perfect lap sessions.';
                    color = 'text-yellow-600';
                } else {
                    grade = 'Needs Patience';
                    message = 'Take it slow and respect boundaries.';
                    color = 'text-red-600';
                }

                const analysisHtml = `
                    <div class="flex items-center justify-between mb-3">
                        <span class="text-lg font-bold ${color}">${grade}</span>
                        <span class="text-2xl font-bold ${color}">${compatibility}%</span>
                    </div>
                    <p class="text-gray-600 text-sm">${message}</p>
                    <div class="mt-3 bg-gray-200 rounded-full h-3">
                        <div class="bg-gradient-to-r from-red-500 via-yellow-500 to-green-500 h-3 rounded-full" style="width: ${compatibility}%"></div>
                    </div>
                `;

                document.getElementById('matchAnalysis').innerHTML = analysisHtml;
            }

            updateLapTips() {
                const tipsHtml = this.lapSchedule.tips.map(tip => `
                    <div class="flex items-start space-x-3">
                        <i class="${tip.icon} text-blue-500 mt-1" aria-hidden="true"></i>
                        <p class="text-sm text-gray-700">${tip.text}</p>
                    </div>
                `).join('');

                document.getElementById('lapTips').innerHTML = tipsHtml;
            }

            updateWeeklySchedule() {
                const scheduleHtml = this.lapSchedule.weeklySchedule.map(day => {
                    const bgColor = day.isLapDay ? 'bg-amber-100 border-amber-300' : 'bg-gray-50 border-gray-200';
                    const textColor = day.isLapDay ? 'text-amber-800' : 'text-gray-600';
                    
