                        <div class="p-3 rounded-lg border-2 ${bgColor} text-center">
                            <div class="font-semibold ${textColor} mb-1">${day.day}</div>
                            <div class="text-xs ${textColor}">${day.activity}</div>
                        </div>
                    `;
                }).join('');

                document.getElementById('weeklySchedule').innerHTML = scheduleHtml;
            }

