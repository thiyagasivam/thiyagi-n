            shareResults() {
                const dogName = this.dogProfile.name;
                const lapDays = this.lapSchedule.lapDaysPerWeek;
                const duration = this.lapSchedule.sessionDuration;
                const compatibility = this.lapSchedule.compatibility;

                const shareText = `ðŸ• ${dogName} needs ${lapDays} lap days per week (${duration} min each)! Our compatibility: ${compatibility}%. Calculate your dog's lap schedule: ${window.location.href}`;

                if (navigator.share) {
                    navigator.share({
                        title: `${dogName}'s Lap Day Schedule`,
                        text: shareText
                    });
                } else {
                    navigator.clipboard.writeText(shareText).then(() => {
                        alert('Lap schedule copied to clipboard!');
                    });
                }
            }

            saveSchedule() {
                const scheduleData = {
                    dogProfile: this.dogProfile,
                    lapSchedule: this.lapSchedule,
                    generatedDate: new Date().toISOString()
                };
                
                const blob = new Blob([JSON.stringify(scheduleData, null, 2)], { type: 'application/json' });
                const url = URL.createObjectURL(blob);
                const a = document.createElement('a');
                a.href = url;
                a.download = `${this.dogProfile.name.replace(/\s+/g, '_')}_lap_schedule.json`;
                a.click();
                URL.revokeObjectURL(url);
            }

