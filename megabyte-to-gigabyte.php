<script>
function updateConversion() {
  const megabyteValue = parseFloat(document.getElementById('megabyteValue').value);
  const gigabyteResult = document.getElementById('gigabyteResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(megabyteValue) || megabyteValue < 0) {
    gigabyteResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert megabytes to gigabytes
  // 1 gigabyte = 1,024 megabytes
  const gigabyteValue = megabyteValue / 1024;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (gigabyteValue >= 1000) {
    formattedResult = gigabyteValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (gigabyteValue >= 100) {
    formattedResult = gigabyteValue.toFixed(2);
  } else if (gigabyteValue >= 10) {
    formattedResult = gigabyteValue.toFixed(3);
  } else if (gigabyteValue >= 1) {
    formattedResult = gigabyteValue.toFixed(4);
  } else {
    formattedResult = gigabyteValue.toFixed(6);
  }
  
  gigabyteResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const megabyteValue = document.getElementById('megabyteValue').value;
  const gigabyteResult = document.getElementById('gigabyteResult').value;
  const copyText = `${megabyteValue} megabytes = ${gigabyteResult} gigabytes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('megabyteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('megabyteValue').focus();
  document.getElementById('megabyteValue').value = 1024;
  updateConversion();
});
</script>

