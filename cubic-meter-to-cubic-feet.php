<script>
function updateConversion() {
  const cubicMeterValue = parseFloat(document.getElementById('cubicMeterValue').value);
  const cubicFeetResult = document.getElementById('cubicFeetResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(cubicMeterValue) || cubicMeterValue < 0) {
    cubicFeetResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cubic meters to cubic feet (1 m³ = 35.3147 ft³)
  const cubicFeetValue = cubicMeterValue * 35.3147;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (cubicFeetValue >= 1000) {
    formattedResult = cubicFeetValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (cubicFeetValue >= 100) {
    formattedResult = cubicFeetValue.toFixed(2);
  } else if (cubicFeetValue >= 10) {
    formattedResult = cubicFeetValue.toFixed(3);
  } else {
    formattedResult = cubicFeetValue.toFixed(4);
  }
  
  cubicFeetResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const cubicMeterValue = document.getElementById('cubicMeterValue').value;
  const cubicFeetResult = document.getElementById('cubicFeetResult').value;
  const copyText = `${cubicMeterValue} cubic meters = ${cubicFeetResult} cubic feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('cubicMeterValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('cubicMeterValue').focus();
  document.getElementById('cubicMeterValue').value = 1;
  updateConversion();
});
</script>

