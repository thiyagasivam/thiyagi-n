<script>
function updateConversion() {
  const quartInput = document.getElementById('quartValue');
  const quartValue = parseFloat(quartInput.value);
  const fluidOunceResult = document.getElementById('fluidOunceResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(quartValue) || quartValue < 0) {
    fluidOunceResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    fluidOunceResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert US quarts to fluid ounces (1 US quart = 32 fluid ounces)
  const fluidOunceValue = quartValue * 32;
  
  let formattedResult = fluidOunceValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 4 
  });
  
  fluidOunceResult.value = formattedResult;
  fluidOunceResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedQuart = quartValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedQuart} qt = ${formattedResult} fl oz`;
  liveConversion.classList.remove('hidden');
  
  quartInput.classList.add('border-sky-500');
}

function copyResult() {
  const quartValue = document.getElementById('quartValue').value;
  const fluidOunceResult = document.getElementById('fluidOunceResult').value;
  const copyText = `${quartValue} qt = ${fluidOunceResult} fl oz`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('quartValue').value = '';
  document.getElementById('fluidOunceResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('fluidOunceResult').classList.remove('result-highlight');
  document.getElementById('quartValue').classList.remove('border-sky-500');
  document.getElementById('quartValue').focus();
}

document.getElementById('quartValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const quartInput = document.getElementById('quartValue');
  quartInput.focus();
  
  setTimeout(() => {
    quartInput.value = 1;
    updateConversion();
  }, 500);
});
</script>

