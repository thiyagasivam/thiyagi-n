<script>
function updateConversion() {
  const byteInput = document.getElementById('byteValue');
  const byteValue = parseFloat(byteInput.value);
  const gigabyteResult = document.getElementById('gigabyteResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(byteValue) || byteValue < 0) {
    gigabyteResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    gigabyteResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert bytes to gigabytes (1 GB = 1,000,000,000 bytes - decimal)
  const gigabyteValue = byteValue / 1000000000;
  
  let formattedResult = gigabyteValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 9 
  });
  
  gigabyteResult.value = formattedResult;
  gigabyteResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedByte = byteValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedByte} B = ${formattedResult} GB`;
  liveConversion.classList.remove('hidden');
  
  byteInput.classList.add('border-violet-500');
}

function copyResult() {
  const byteValue = document.getElementById('byteValue').value;
  const gigabyteResult = document.getElementById('gigabyteResult').value;
  const copyText = `${byteValue} B = ${gigabyteResult} GB`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('byteValue').value = '';
  document.getElementById('gigabyteResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('gigabyteResult').classList.remove('result-highlight');
  document.getElementById('byteValue').classList.remove('border-violet-500');
  document.getElementById('byteValue').focus();
}

document.getElementById('byteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const byteInput = document.getElementById('byteValue');
  byteInput.focus();
  
  setTimeout(() => {
    byteInput.value = 1000000000;
    updateConversion();
  }, 500);
});
</script>

