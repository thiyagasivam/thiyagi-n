<script>
function updateConversion() {
  const hectareInput = document.getElementById('hectareValue');
  const hectareValue = parseFloat(hectareInput.value);
  const acresResult = document.getElementById('acresResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(hectareValue) || hectareValue < 0) {
    acresResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    acresResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert hectares to acres (1 hectare = 2.47105 acres)
  const acresValue = hectareValue * 2.47105;
  
  let formattedResult = acresValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  acresResult.value = formattedResult;
  acresResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedHectares = hectareValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedHectares} ha = ${formattedResult} ac`;
  liveConversion.classList.remove('hidden');
  
  hectareInput.classList.add('border-emerald-500');
}

function copyResult() {
  const hectareValue = document.getElementById('hectareValue').value;
  const acresResult = document.getElementById('acresResult').value;
  const copyText = `${hectareValue} ha = ${acresResult} ac`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('hectareValue').value = '';
  document.getElementById('acresResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('acresResult').classList.remove('result-highlight');
  document.getElementById('hectareValue').classList.remove('border-emerald-500');
  document.getElementById('hectareValue').focus();
}

document.getElementById('hectareValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const hectareInput = document.getElementById('hectareValue');
  hectareInput.focus();
  
  setTimeout(() => {
    hectareInput.value = 2.5;
    updateConversion();
  }, 500);
});
</script>

