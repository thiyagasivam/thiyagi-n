<script>
function updateConversion() {
  const kelvinValue = parseFloat(document.getElementById('kelvinValue').value);
  const fahrenheitResult = document.getElementById('fahrenheitResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kelvinValue) || kelvinValue < 0) {
    fahrenheitResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert Kelvin to Fahrenheit: °F = (K - 273.15) × 9/5 + 32
  const fahrenheitValue = (kelvinValue - 273.15) * (9/5) + 32;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (Math.abs(fahrenheitValue) >= 1000) {
    formattedResult = fahrenheitValue.toFixed(1);
  } else if (Math.abs(fahrenheitValue) >= 100) {
    formattedResult = fahrenheitValue.toFixed(2);
  } else {
    formattedResult = fahrenheitValue.toFixed(3);
  }
  
  fahrenheitResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kelvinValue = document.getElementById('kelvinValue').value;
  const fahrenheitResult = document.getElementById('fahrenheitResult').value;
  const copyText = `${kelvinValue} K = ${fahrenheitResult}°F`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kelvinValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kelvinValue').focus();
  document.getElementById('kelvinValue').value = 298.15;
  updateConversion();
});
</script>

