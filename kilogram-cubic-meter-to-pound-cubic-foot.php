let isUpdating = false;

function convertKgM3ToLbFt3(kgm3) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(kgm3) && kgm3 !== '') {
        const lbft3 = kgm3 * 0.062427960576145;
        document.getElementById('lbft3Input').value = lbft3.toFixed(10);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-stone-600 mr-2"></i>
            <strong>${kgm3} kg/m³ = ${lbft3.toFixed(6)} lb/ft³</strong>
        `;
    } else {
        document.getElementById('lbft3Input').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertLbFt3ToKgM3(lbft3) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(lbft3) && lbft3 !== '') {
        const kgm3 = lbft3 / 0.062427960576145;
        document.getElementById('kgm3Input').value = kgm3.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-stone-600 mr-2"></i>
            <strong>${lbft3} lb/ft³ = ${kgm3.toFixed(3)} kg/m³</strong>
        `;
    } else {
        document.getElementById('kgm3Input').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('kgm3Input').value = '';
    document.getElementById('lbft3Input').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const kgm3Value = document.getElementById('kgm3Input').value;
    const lbft3Value = document.getElementById('lbft3Input').value;
    
    document.getElementById('kgm3Input').value = lbft3Value;
    document.getElementById('lbft3Input').value = kgm3Value;
    
    if (lbft3Value) {
        convertKgM3ToLbFt3(parseFloat(lbft3Value));
    } else if (kgm3Value) {
        convertLbFt3ToKgM3(parseFloat(kgm3Value));
    }
}

// Add event listeners
document.getElementById('kgm3Input').addEventListener('input', function() {
    convertKgM3ToLbFt3(parseFloat(this.value));
});

document.getElementById('lbft3Input').addEventListener('input', function() {
    convertLbFt3ToKgM3(parseFloat(this.value));
});
</script>

