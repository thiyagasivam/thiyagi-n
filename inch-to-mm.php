<script>
function updateConversion() {
  const inchValue = parseFloat(document.getElementById('inchValue').value);
  const mmResult = document.getElementById('mmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(inchValue) || inchValue < 0) {
    mmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert inches to millimeters (1 inch = 25.4 mm)
  const mmValue = inchValue * 25.4;
  
  // Format the result
  let formattedResult;
  if (mmValue >= 1000) {
    formattedResult = mmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (mmValue >= 100) {
    formattedResult = mmValue.toFixed(2);
  } else if (mmValue >= 10) {
    formattedResult = mmValue.toFixed(3);
  } else {
    formattedResult = mmValue.toFixed(4);
  }
  
  mmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const inchValue = document.getElementById('inchValue').value;
  const mmResult = document.getElementById('mmResult').value;
  const copyText = `${inchValue} inches = ${mmResult} millimeters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('inchValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('inchValue').focus();
  document.getElementById('inchValue').value = 1;
  updateConversion();
});
</script>

