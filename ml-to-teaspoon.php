<script>
let currentUnit = 'us'; // 'us' or 'metric'

function updateConversion() {
  const mlValue = parseFloat(document.getElementById('mlValue').value);
  const teaspoonResult = document.getElementById('teaspoonResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mlValue) || mlValue < 0) {
    teaspoonResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert milliliters to teaspoons based on current unit type
  let teaspoonValue;
  if (currentUnit === 'us') {
    // 1 milliliter = 0.203 US teaspoons
    teaspoonValue = mlValue * 0.203;
  } else {
    // 1 milliliter = 0.2 metric teaspoons
    teaspoonValue = mlValue * 0.2;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (teaspoonValue >= 10) {
    formattedResult = teaspoonValue.toFixed(1);
  } else if (teaspoonValue >= 1) {
    formattedResult = teaspoonValue.toFixed(2);
  } else {
    formattedResult = teaspoonValue.toFixed(3);
  }
  
  teaspoonResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mlValue = document.getElementById('mlValue').value;
  const teaspoonResult = document.getElementById('teaspoonResult').value;
  const unitType = currentUnit === 'us' ? 'US teaspoons' : 'metric teaspoons';
  const copyText = `${mlValue} milliliters = ${teaspoonResult} ${unitType}`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usTeaspoonBtn');
  const metricBtn = document.getElementById('metricTeaspoonBtn');
  const teaspoonLabel = document.getElementById('teaspoonLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    teaspoonLabel.textContent = 'US Teaspoons';
  } else {
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    teaspoonLabel.textContent = 'Metric Teaspoons';
  }
  
  updateConversion();
}

document.getElementById('mlValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usTeaspoonBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('metricTeaspoonBtn').addEventListener('click', () => switchUnit('metric'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mlValue').focus();
  document.getElementById('mlValue').value = 10;
  updateConversion();
});
