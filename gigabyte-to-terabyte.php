<script>
function updateConversion() {
  const gigabyteValue = parseFloat(document.getElementById('gigabyteValue').value);
  const terabyteResult = document.getElementById('terabyteResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(gigabyteValue) || gigabyteValue < 0) {
    terabyteResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert gigabytes to terabytes
  // 1 terabyte = 1,024 gigabytes
  const terabyteValue = gigabyteValue / 1024;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (terabyteValue >= 1000) {
    formattedResult = terabyteValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else if (terabyteValue >= 100) {
    formattedResult = terabyteValue.toFixed(2);
  } else if (terabyteValue >= 10) {
    formattedResult = terabyteValue.toFixed(3);
  } else if (terabyteValue >= 1) {
    formattedResult = terabyteValue.toFixed(4);
  } else {
    formattedResult = terabyteValue.toFixed(6);
  }
  
  terabyteResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const gigabyteValue = document.getElementById('gigabyteValue').value;
  const terabyteResult = document.getElementById('terabyteResult').value;
  const copyText = `${gigabyteValue} gigabytes = ${terabyteResult} terabytes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('gigabyteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('gigabyteValue').focus();
  document.getElementById('gigabyteValue').value = 1024;
  updateConversion();
});
</script>

