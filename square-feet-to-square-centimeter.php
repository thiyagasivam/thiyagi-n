<script>
function updateConversion() {
  const sqFtValue = parseFloat(document.getElementById('sqFtValue').value);
  const sqCmResult = document.getElementById('sqCmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqFtValue) || sqFtValue < 0) {
    sqCmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square feet to square centimeters (1 sq ft = 929.0304 sq cm)
  const sqCmValue = sqFtValue * 929.0304;
  
  // Format the result with appropriate decimal places and thousands separators
  let formattedResult;
  if (sqCmValue >= 100000) {
    formattedResult = sqCmValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (sqCmValue >= 10000) {
    formattedResult = sqCmValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else if (sqCmValue >= 1000) {
    formattedResult = sqCmValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  } else {
    formattedResult = sqCmValue.toFixed(3);
  }
  
  sqCmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqFtValue = document.getElementById('sqFtValue').value;
  const sqCmResult = document.getElementById('sqCmResult').value;
  const copyText = `${sqFtValue} square feet = ${sqCmResult} square centimeters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqFtValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqFtValue').focus();
  document.getElementById('sqFtValue').value = 1;
  updateConversion();
});
</script>

