function updateConversion() {
  const mphValue = parseFloat(document.getElementById('mphValue').value);
  const kphResult = document.getElementById('kphResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mphValue) || mphValue < 0) {
    kphResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert mph to kph (1 mph = 1.609344 kph)
  const kphValue = mphValue * 1.609344;
  
  // Format the result
  let formattedResult;
  if (kphValue >= 1000) {
    formattedResult = kphValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (kphValue >= 1) {
    formattedResult = kphValue.toFixed(4);
  } else {
    formattedResult = kphValue.toFixed(6);
  }
  
  kphResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mphValue = document.getElementById('mphValue').value;
  const kphResult = document.getElementById('kphResult').value;
  const copyText = `${mphValue} mph = ${kphResult} kph`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('mphValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mphValue').focus();
  document.getElementById('mphValue').value = 60;
  updateConversion();
});
</script>

