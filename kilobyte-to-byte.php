<script>
function updateConversion() {
  const kilobyteValue = parseFloat(document.getElementById('kilobyteValue').value);
  const byteResult = document.getElementById('byteResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kilobyteValue) || kilobyteValue < 0) {
    byteResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert kilobytes to bytes
  // 1 kilobyte = 1,024 bytes
  const byteValue = kilobyteValue * 1024;
  
  // Format the result with commas for large numbers
  const formattedResult = byteValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  
  byteResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kilobyteValue = document.getElementById('kilobyteValue').value;
  const byteResult = document.getElementById('byteResult').value;
  const copyText = `${kilobyteValue} kilobytes = ${byteResult} bytes`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kilobyteValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kilobyteValue').focus();
  document.getElementById('kilobyteValue').value = 1;
  updateConversion();
});
</script>

