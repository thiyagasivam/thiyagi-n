<script>
  // Conversion function: 1 meter = 39.3701 inches
  function convertMeterToInches() {
    const meterInput = document.getElementById('meterValue');
    const inchesResult = document.getElementById('inchesResult');
    const copyBtn = document.getElementById('copyBtn');
    
    const meterValue = parseFloat(meterInput.value);
    
    if (isNaN(meterValue) || meterValue < 0) {
      inchesResult.value = '';
      copyBtn.classList.add('hidden');
      return;
    }
    
    const inchesValue = meterValue * 39.3701;
    inchesResult.value = inchesValue.toFixed(6);
    copyBtn.classList.remove('hidden');
  }
  
  // Copy to clipboard function
  function copyResult() {
    const inchesResult = document.getElementById('inchesResult');
    const copySuccess = document.getElementById('copySuccess');
    
    if (inchesResult.value) {
      navigator.clipboard.writeText(inchesResult.value).then(() => {
        copySuccess.classList.remove('hidden');
        setTimeout(() => {
          copySuccess.classList.add('hidden');
        }, 2000);
      });
    }
  }
  
  // Event listeners
  document.getElementById('meterValue').addEventListener('input', convertMeterToInches);
  document.getElementById('copyBtn').addEventListener('click', copyResult);
  
  // Initialize conversion
  convertMeterToInches();
</script>

