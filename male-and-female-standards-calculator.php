            switchGender(gender) {
                this.currentGender = gender;
                
                // Update tab appearance
                document.querySelectorAll('.gender-tab').forEach(tab => {
                    tab.classList.remove('active');
                });
                document.getElementById(gender + 'Tab').classList.add('active');
                
