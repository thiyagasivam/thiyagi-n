            generateRecommendations(data) {
                const recommendations = [];
                const bmiCategory = this.getBMICategory(data.bmi);

                // BMI-based recommendations
                if (bmiCategory.category === 'Underweight') {
                    recommendations.push('Consider consulting a healthcare provider about healthy weight gain strategies.');
                    recommendations.push('Focus on nutrient-dense, calorie-rich foods and strength training.');
                } else if (bmiCategory.category === 'Overweight' || bmiCategory.category === 'Obese') {
                    recommendations.push('Consider a structured weight loss plan combining diet and exercise.');
                    recommendations.push('Aim for 1-2 pounds of weight loss per week through a calorie deficit.');
                } else {
                    recommendations.push('Great job maintaining a healthy BMI! Continue your current lifestyle.');
                }

                // Weight difference recommendations
                const weightDiff = Math.abs(data.weightDifference);
                if (weightDiff > 5) {
                    if (data.weightDifference > 0) {
                        recommendations.push(`You're ${weightDiff.toFixed(1)} kg above your ideal weight. Focus on gradual weight reduction.`);
                    } else {
                        recommendations.push(`You're ${weightDiff.toFixed(1)} kg below your ideal weight. Consider healthy weight gain.`);
                    }
                }

                // Activity recommendations
                recommendations.push('Aim for at least 150 minutes of moderate-intensity exercise per week.');
                recommendations.push('Include strength training exercises at least 2 days per week.');

                // Gender-specific recommendations
                if (data.gender === 'female') {
                    recommendations.push('Ensure adequate calcium and iron intake to support bone and blood health.');
                    if (data.age >= 40) {
                        recommendations.push('Consider discussing hormone changes with your healthcare provider.');
                    }
