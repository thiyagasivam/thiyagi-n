            calculateStandards() {
                const age = parseInt(document.getElementById('age').value);
                const height = parseFloat(document.getElementById('height').value);
                const weight = parseFloat(document.getElementById('weight').value);
                const waist = parseFloat(document.getElementById('waist').value) || null;
                const hip = parseFloat(document.getElementById('hip').value) || null;
                const bodyFat = parseFloat(document.getElementById('bodyFat').value) || null;
                const activityLevel = document.getElementById('activityLevel').value;
                const goalType = document.getElementById('goalType').value;

                if (!age || !height || !weight) {
                    alert('Please fill in age, height, and weight fields.');
                    return;
                }

                // Calculate BMI
                const bmi = this.calculateBMI(weight, height);
                
                // Calculate Ideal Weight
                const idealWeight = this.calculateIdealWeight(height, this.currentGender);
                
                // Calculate Body Fat Range
                const bodyFatRange = this.getBodyFatRange(age, this.currentGender);
                
                // Calculate WHR if measurements provided
                const whr = (waist && hip) ? this.calculateWHR(waist, hip) : null;
                
                // Calculate Daily Calories
                const calories = this.calculateCalories(weight, height, age, this.currentGender, activityLevel, goalType);

                // Display results
                this.displayResults({
                    bmi,
                    idealWeight,
                    weightDifference: weight - idealWeight,
                    bodyFatRange,
                    whr,
                    calories,
                    age,
                    gender: this.currentGender,
                    goalType
                });
            }

            calculateBMI(weight, height) {
                const heightInMeters = height / 100;
                return weight / (heightInMeters * heightInMeters);
            }

            calculateIdealWeight(height, gender) {
                if (gender === 'male') {
                    return (height - 100) - ((height - 150) / 4);
                } else {
                    return (height - 100) - ((height - 150) / 2.5);
                }
            }

            getBodyFatRange(age, gender) {
                if (gender === 'male') {
                    if (age <= 30) return { min: 15, max: 20, category: 'Healthy' };
                    if (age <= 50) return { min: 16, max: 23, category: 'Healthy' };
                    return { min: 17, max: 25, category: 'Healthy' };
                } else {
                    if (age <= 30) return { min: 20, max: 25, category: 'Healthy' };
                    if (age <= 50) return { min: 23, max: 28, category: 'Healthy' };
                    return { min: 25, max: 30, category: 'Healthy' };
                }
            }

            calculateWHR(waist, hip) {
                return waist / hip;
            }

            calculateCalories(weight, height, age, gender, activityLevel, goalType) {
                // Calculate BMR using Mifflin-St Jeor Equation
                let bmr;
                if (gender === 'male') {
                    bmr = 10 * weight + 6.25 * height - 5 * age + 5;
                } else {
                    bmr = 10 * weight + 6.25 * height - 5 * age - 161;
                }

                // Apply activity multiplier
                const activityMultipliers = {
                    sedentary: 1.2,
                    light: 1.375,
                    moderate: 1.55,
                    very: 1.725,
                    extra: 1.9
                };

                let tdee = bmr * activityMultipliers[activityLevel];

                // Adjust for goal
                switch (goalType) {
                    case 'lose':
                        tdee -= 500; // 1 lb per week loss
                        break;
                    case 'gain':
                        tdee += 500; // 1 lb per week gain
                        break;
                }

                return Math.round(tdee);
            }

            getBMICategory(bmi) {
                if (bmi < 18.5) return { category: 'Underweight', color: 'text-blue-600', progress: 25 };
                if (bmi < 25) return { category: 'Normal', color: 'text-green-600', progress: 50 };
                if (bmi < 30) return { category: 'Overweight', color: 'text-yellow-600', progress: 75 };
                return { category: 'Obese', color: 'text-red-600', progress: 100 };
            }

            getWHRCategory(whr, gender) {
                if (gender === 'male') {
                    if (whr < 0.85) return 'Excellent';
                    if (whr < 0.90) return 'Good';
                    if (whr < 0.95) return 'Average';
                    return 'At Risk';
                } else {
                    if (whr < 0.75) return 'Excellent';
                    if (whr < 0.80) return 'Good';
                    if (whr < 0.86) return 'Average';
                    return 'At Risk';
                }
            }

