                const weightDiff = data.weightDifference;
                const weightDiffText = weightDiff > 0 ? `${weightDiff.toFixed(1)} kg over` : `${Math.abs(weightDiff).toFixed(1)} kg under`;
                document.getElementById('weightDifference').textContent = weightDiffText;
                const weightProgress = Math.min(100, Math.max(0, 100 - Math.abs(weightDiff) * 2));
                document.getElementById('weightProgress').style.width = `${weightProgress}%`;

                // Body Fat Results
