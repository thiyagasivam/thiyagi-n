                document.getElementById('bodyFatCategory').textContent = `${data.bodyFatRange.category} Range`;
                document.getElementById('fatProgress').style.width = '70%';

                // WHR Results
                if (data.whr) {
                    const whrCategory = this.getWHRCategory(data.whr, data.gender);
                    document.getElementById('whrResult').textContent = data.whr.toFixed(2);
