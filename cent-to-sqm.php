<script>
function updateConversion() {
  const centValue = parseFloat(document.getElementById('centValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(centValue) || centValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert cent to square meters (1 cent = 40.468564224 square meters)
  const sqmValue = centValue * 40.468564224;
  
  // Format the result
  let formattedResult;
  if (sqmValue >= 10000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqmValue >= 100) {
    formattedResult = sqmValue.toFixed(1);
  } else {
    formattedResult = sqmValue.toFixed(2);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const centValue = document.getElementById('centValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${centValue} cents = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('centValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('centValue').focus();
  document.getElementById('centValue').value = 10;
  updateConversion();
});
</script>

