<script>
function updateConversion() {
  const kgValue = parseFloat(document.getElementById('kgValue').value);
  const gramResult = document.getElementById('gramResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kgValue) || kgValue < 0) {
    gramResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert kilograms to grams (1 kg = 1000 g)
  const gramValue = kgValue * 1000;
  
  // Format the result
  let formattedResult;
  if (gramValue >= 1000000) {
    formattedResult = gramValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (gramValue >= 100000) {
    formattedResult = gramValue.toLocaleString('en-US', { 
      minimumFractionDigits: 1, 
      maximumFractionDigits: 1 
    });
  } else if (gramValue >= 10000) {
    formattedResult = gramValue.toFixed(1);
  } else if (gramValue >= 1000) {
    formattedResult = gramValue.toFixed(2);
  } else {
    formattedResult = gramValue.toFixed(3);
  }
  
  gramResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kgValue = document.getElementById('kgValue').value;
  const gramResult = document.getElementById('gramResult').value;
  const copyText = `${kgValue} kilograms = ${gramResult} grams`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('kgValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kgValue').focus();
  document.getElementById('kgValue').value = 2.5;
  updateConversion();
});
</script>

