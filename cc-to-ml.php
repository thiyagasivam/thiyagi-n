// Enhanced conversion function
function updateConversion() {
  const ccInput = document.getElementById('ccValue');
  const ccValue = parseFloat(ccInput.value);
  const mlResult = document.getElementById('mlResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Clear result if input is invalid
  if (isNaN(ccValue) || ccValue < 0) {
    mlResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    mlResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert cc to ml (1 cc = 1 ml exactly)
  const mlValue = ccValue;
  
  // Format result
  let formattedResult = mlValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  // Update result field
  mlResult.value = formattedResult;
  mlResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  // Update live conversion display
  const formattedCC = ccValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedCC} cc = ${formattedResult} ml`;
  liveConversion.classList.remove('hidden');
  
  ccInput.classList.add('border-emerald-500');
}

// Copy function
function copyResult() {
  const ccValue = document.getElementById('ccValue').value;
  const mlResult = document.getElementById('mlResult').value;
  const copyText = `${ccValue} cc = ${mlResult} ml`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

// Show copy success message
function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

// Clear function
function clearAll() {
  document.getElementById('ccValue').value = '';
  document.getElementById('mlResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('mlResult').classList.remove('result-highlight');
  document.getElementById('ccValue').classList.remove('border-emerald-500');
  document.getElementById('ccValue').focus();
}

// Event listeners
document.getElementById('ccValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

// Auto-focus and demo value on page load
window.addEventListener('DOMContentLoaded', () => {
  const ccInput = document.getElementById('ccValue');
  ccInput.focus();
  
  setTimeout(() => {
    ccInput.value = 100;
    updateConversion();
  }, 500);
});
