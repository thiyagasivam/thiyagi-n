<script>
function updateConversion() {
  const pixelInput = document.getElementById('pixelValue');
  const pixelValue = parseFloat(pixelInput.value);
  const dpiSelect = document.getElementById('dpiValue');
  const customDpiInput = document.getElementById('customDpi');
  const inchResult = document.getElementById('inchResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  // Get DPI value
  let dpiValue;
  if (dpiSelect.value === 'custom') {
    dpiValue = parseFloat(customDpiInput.value);
  } else {
    dpiValue = parseFloat(dpiSelect.value);
  }
  
  if (isNaN(pixelValue) || pixelValue < 0 || isNaN(dpiValue) || dpiValue <= 0) {
    inchResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    inchResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert pixels to inches (pixels / DPI = inches)
  const inchValue = pixelValue / dpiValue;
  
  let formattedResult = inchValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  inchResult.value = formattedResult;
  inchResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedPixels = pixelValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedPixels} px @ ${dpiValue} DPI = ${formattedResult} inches`;
  liveConversion.classList.remove('hidden');
  
  pixelInput.classList.add('border-emerald-500');
}

function toggleCustomDpi() {
  const dpiSelect = document.getElementById('dpiValue');
  const customDpiInput = document.getElementById('customDpiInput');
  
  if (dpiSelect.value === 'custom') {
    customDpiInput.classList.remove('hidden');
    document.getElementById('customDpi').focus();
  } else {
    customDpiInput.classList.add('hidden');
    updateConversion();
  }
}

function copyResult() {
  const pixelValue = document.getElementById('pixelValue').value;
  const dpiSelect = document.getElementById('dpiValue');
  const customDpiInput = document.getElementById('customDpi');
  const inchResult = document.getElementById('inchResult').value;
  
  let dpiValue;
  if (dpiSelect.value === 'custom') {
    dpiValue = customDpiInput.value;
  } else {
    dpiValue = dpiSelect.value;
  }
  
  const copyText = `${pixelValue} px @ ${dpiValue} DPI = ${inchResult} inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('pixelValue').value = '';
  document.getElementById('dpiValue').selectedIndex = 1; // Reset to 96 DPI
  document.getElementById('customDpi').value = '';
  document.getElementById('customDpiInput').classList.add('hidden');
  document.getElementById('inchResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('inchResult').classList.remove('result-highlight');
  document.getElementById('pixelValue').classList.remove('border-emerald-500');
  document.getElementById('pixelValue').focus();
}

document.getElementById('pixelValue').addEventListener('input', updateConversion);
document.getElementById('dpiValue').addEventListener('change', toggleCustomDpi);
document.getElementById('customDpi').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const pixelInput = document.getElementById('pixelValue');
  pixelInput.focus();
  
  setTimeout(() => {
    pixelInput.value = 960;
    updateConversion();
  }, 500);
});
</script>

