<script>
function updateConversion() {
  const cubicInchInput = document.getElementById('cubicInchValue');
  const cubicInchValue = parseFloat(cubicInchInput.value);
  const literResult = document.getElementById('literResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(cubicInchValue) || cubicInchValue < 0) {
    literResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    literResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert cubic inches to liters (1 cubic inch = 0.0163871 liters)
  const literValue = cubicInchValue * 0.0163871;
  
  let formattedResult = literValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  literResult.value = formattedResult;
  literResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedCubicInch = cubicInchValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedCubicInch} in³ = ${formattedResult} L`;
  liveConversion.classList.remove('hidden');
  
  cubicInchInput.classList.add('border-sky-500');
}

function copyResult() {
  const cubicInchValue = document.getElementById('cubicInchValue').value;
  const literResult = document.getElementById('literResult').value;
  const copyText = `${cubicInchValue} in³ = ${literResult} L`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('cubicInchValue').value = '';
  document.getElementById('literResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('literResult').classList.remove('result-highlight');
  document.getElementById('cubicInchValue').classList.remove('border-sky-500');
  document.getElementById('cubicInchValue').focus();
}

document.getElementById('cubicInchValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const cubicInchInput = document.getElementById('cubicInchValue');
  cubicInchInput.focus();
  
  setTimeout(() => {
    cubicInchInput.value = 61.024;
    updateConversion();
  }, 500);
});
</script>

