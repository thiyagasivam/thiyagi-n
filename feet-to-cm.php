<script>
function updateConversion() {
  const feetValue = parseFloat(document.getElementById('feetValue').value);
  const cmResult = document.getElementById('cmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(feetValue) || feetValue < 0) {
    cmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert feet to centimeters (1 foot = 30.48 centimeters)
  const cmValue = feetValue * 30.48;
  
  // Format the result
  let formattedResult;
  if (cmValue >= 1000) {
    formattedResult = cmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (cmValue >= 10) {
    formattedResult = cmValue.toFixed(2);
  } else {
    formattedResult = cmValue.toFixed(3);
  }
  
  cmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const feetValue = document.getElementById('feetValue').value;
  const cmResult = document.getElementById('cmResult').value;
  const copyText = `${feetValue} feet = ${cmResult} centimeters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('feetValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('feetValue').focus();
  document.getElementById('feetValue').value = 8;
  updateConversion();
});
</script>

