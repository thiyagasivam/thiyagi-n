    <script>
        class TextToSlugConverter {
            constructor() {
                this.slugHistory = [];
                this.init();
            }

            init() {
                this.bindEvents();
                this.updateCharCount();
            }

            bindEvents() {
                document.getElementById('inputText').addEventListener('input', () => {
                    this.updateCharCount();
                    this.autoConvert();
                });
                document.getElementById('convertBtn').addEventListener('click', () => this.convertToSlug());
                document.getElementById('clearBtn').addEventListener('click', () => this.clearInput());
                document.getElementById('copyBtn').addEventListener('click', () => this.copySlug());
                document.getElementById('newSlugBtn')?.addEventListener('click', () => this.newSlug());
                document.getElementById('downloadBtn')?.addEventListener('click', () => this.downloadHistory());
                document.getElementById('shareBtn')?.addEventListener('click', () => this.shareTool());

                // Option cards interaction
                document.querySelectorAll('.option-card').forEach(card => {
                    card.addEventListener('click', () => {
                        if (card.dataset.option === 'separator') {
                            card.classList.add('active');
                            document.querySelectorAll('[data-option="separator"]').forEach(other => {
                                if (other !== card) other.classList.remove('active');
                            });
                        }
                        this.autoConvert();
                    });
                });

                // Settings change listeners
                document.querySelectorAll('input[type="radio"], input[type="checkbox"], select').forEach(input => {
                    input.addEventListener('change', () => this.autoConvert());
                });

                // Example cards
                document.querySelectorAll('.example-card').forEach(card => {
                    card.addEventListener('click', () => {
                        document.getElementById('inputText').value = card.dataset.example;
                        this.updateCharCount();
                        this.convertToSlug();
                    });
                });
            }

            updateCharCount() {
                const text = document.getElementById('inputText').value;
                document.getElementById('charCount').textContent = `${text.length} characters`;
            }

            autoConvert() {
                const text = document.getElementById('inputText').value.trim();
                if (text.length > 0) {
                    this.convertToSlug(false);
                }
            }

            convertToSlug(showResults = true) {
                const text = document.getElementById('inputText').value.trim();
                
                if (!text) {
                    alert('Please enter some text to convert.');
                    return;
                }

                const options = this.getOptions();
                const slug = this.generateSlug(text, options);

                if (showResults) {
                    this.displayResults(slug, text);
                    this.addToHistory(text, slug, options);
                }

                return slug;
            }

            getOptions() {
                return {
                    separator: document.querySelector('input[name="separator"]:checked').value,
                    preserveCase: document.getElementById('preserveCase').checked,
                    maxLength: parseInt(document.getElementById('maxLength').value),
                    removeNumbers: document.getElementById('removeNumbers').checked,
                    strictMode: document.getElementById('strictMode').checked
                };
            }

            generateSlug(text, options) {
                let slug = text;

                // Remove HTML tags if any
                slug = slug.replace(/<[^>]*>/g, '');

                // Handle special characters and accents
                slug = this.normalizeText(slug);

                // Remove numbers if requested
                if (options.removeNumbers) {
                    slug = slug.replace(/\d+/g, '');
                }

                // Convert to lowercase unless preserving case
                if (!options.preserveCase) {
                    slug = slug.toLowerCase();
                }

                // Replace spaces and multiple whitespace with separator
                const separator = options.separator === 'underscore' ? '_' : '-';
                slug = slug.replace(/\s+/g, separator);

                // Remove special characters (keep only letters, numbers, separators)
                if (options.strictMode) {
                    slug = slug.replace(/[^a-zA-Z0-9\-_]/g, '');
                } else {
                    slug = slug.replace(/[^\w\-_]/g, '');
                }

                // Replace multiple separators with single separator
                const separatorRegex = new RegExp(`[${separator}]+`, 'g');
                slug = slug.replace(separatorRegex, separator);

                // Remove leading and trailing separators
                slug = slug.replace(new RegExp(`^[${separator}]+|[${separator}]+$`, 'g'), '');

                // Apply length limit
                if (options.maxLength > 0 && slug.length > options.maxLength) {
                    slug = slug.substring(0, options.maxLength);
                    // Remove trailing separator if cut off
                    slug = slug.replace(new RegExp(`[${separator}]+$`), '');
                }

                return slug || 'untitled-slug';
            }

            normalizeText(text) {
                // Normalize accented characters
                return text
                    .normalize('NFD')
                    .replace(/[\u0300-\u036f]/g, '')
                    // Replace common special characters
                    .replace(/[&]/g, 'and')
                    .replace(/[%]/g, 'percent')
                    .replace(/[@]/g, 'at')
                    .replace(/[+]/g, 'plus')
                    .replace(/[#]/g, 'hash')
                    .replace(/[\$]/g, 'dollar');
            }

            displayResults(slug, originalText) {
                const resultsSection = document.getElementById('resultsSection');
                resultsSection.classList.remove('hidden');

                // Update slug result
                document.getElementById('slugResult').textContent = slug;
                document.getElementById('slugLength').textContent = slug.length;

                // Update previews
                document.getElementById('wpPreview').textContent = slug;
                document.getElementById('blogPreview').textContent = slug;
                document.getElementById('productPreview').textContent = slug;

                // Scroll to results
                resultsSection.scrollIntoView({ behavior: 'smooth' });
            }

            addToHistory(originalText, slug, options) {
                const historyItem = {
                    timestamp: new Date(),
                    original: originalText,
                    slug: slug,
                    options: options
                };
                
                this.slugHistory.unshift(historyItem);
                // Keep only last 50 items
                if (this.slugHistory.length > 50) {
                    this.slugHistory = this.slugHistory.slice(0, 50);
                }
            }

            copySlug() {
                const slug = document.getElementById('slugResult').textContent;
                
                if (navigator.clipboard) {
                    navigator.clipboard.writeText(slug).then(() => {
                        this.showCopyStatus();
                    });
                } else {
                    // Fallback for older browsers
                    const textArea = document.createElement('textarea');
                    textArea.value = slug;
                    document.body.appendChild(textArea);
                    textArea.select();
                    document.execCommand('copy');
                    document.body.removeChild(textArea);
                    this.showCopyStatus();
                }
            }

            showCopyStatus() {
                const statusElement = document.getElementById('copyStatus');
                statusElement.classList.remove('hidden');
                setTimeout(() => {
                    statusElement.classList.add('hidden');
                }, 2000);
            }

            clearInput() {
                document.getElementById('inputText').value = '';
                document.getElementById('resultsSection').classList.add('hidden');
                this.updateCharCount();
            }

            newSlug() {
                this.clearInput();
                document.getElementById('inputText').focus();
            }

            downloadHistory() {
                if (this.slugHistory.length === 0) {
                    alert('No slug history to download.');
                    return;
                }

                const content = this.slugHistory.map(item => {
                    return `${item.timestamp.toISOString()}\t${item.original}\t${item.slug}`;
                }).join('\n');

                const blob = new Blob([`Timestamp\tOriginal Text\tGenerated Slug\n${content}`], { type: 'text/plain' });
                const url = URL.createObjectURL(blob);
                const a = document.createElement('a');
                a.href = url;
                a.download = `slug-history-${Date.now()}.txt`;
                a.click();
                URL.revokeObjectURL(url);
            }

            shareTool() {
                if (navigator.share) {
                    navigator.share({
                        title: 'Text to Slug Converter',
                        text: 'Convert text to URL-friendly slugs instantly!',
                        url: window.location.href
                    });
                } else {
                    // Fallback: copy URL to clipboard
                    navigator.clipboard.writeText(window.location.href).then(() => {
                        alert('Tool URL copied to clipboard!');
                    });
                }
            }
        }

        // Initialize converter when page loads
        document.addEventListener('DOMContentLoaded', () => {
            new TextToSlugConverter();
        });

        // Add scroll animations
        document.addEventListener('DOMContentLoaded', () => {
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('fade-in');
                    }
                });
            });

            document.querySelectorAll('section').forEach(el => {
                observer.observe(el);
            });
        });
    </script>
