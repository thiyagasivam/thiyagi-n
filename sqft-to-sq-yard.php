function updateConversion() {
  const sqftValue = parseFloat(document.getElementById('sqftValue').value);
  const sqyardResult = document.getElementById('sqyardResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqftValue) || sqftValue < 0) {
    sqyardResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Conversion factor: 1 sq yard = 9 sq ft, so sq ft ÷ 9 = sq yards
  const sqyardValue = sqftValue / 9;
  
  // Format the result
  let formattedResult;
  if (sqyardValue < 0.01 && sqyardValue !== 0) {
    formattedResult = sqyardValue.toExponential(4);
  } else if (sqyardValue >= 1000) {
    formattedResult = sqyardValue.toLocaleString(undefined, {maximumFractionDigits: 2});
  } else {
    formattedResult = sqyardValue.toLocaleString(undefined, {maximumFractionDigits: 4});
  }
  
  sqyardResult.value = formattedResult + ' sq yards';
  copyBtn.classList.remove('hidden');
}

// Copy to clipboard functionality
function copyResult() {
  const sqftValue = document.getElementById('sqftValue').value;
  const sqyardResult = document.getElementById('sqyardResult').value;
  const copyText = `${sqftValue} square feet = ${sqyardResult}`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqftValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqftValue').focus();
  document.getElementById('sqftValue').value = 90;
  updateConversion();
});
