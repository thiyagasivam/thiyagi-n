function updateConversion() {
  const kmValue = parseFloat(document.getElementById('kmValue').value);
  const milesResult = document.getElementById('milesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(kmValue) || kmValue < 0) {
    milesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert kilometers to miles (1 km = 0.621371 miles)
  const milesValue = kmValue * 0.621371;
  
  // Format the result
  let formattedResult;
  if (milesValue >= 1000) {
    formattedResult = milesValue.toLocaleString('en-US', { 
      minimumFractionDigits: 2, 
      maximumFractionDigits: 2 
    });
  } else if (milesValue >= 1) {
    formattedResult = milesValue.toFixed(4);
  } else {
    formattedResult = milesValue.toFixed(6);
  }
  
  milesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const kmValue = document.getElementById('kmValue').value;
  const milesResult = document.getElementById('milesResult').value;
  const copyText = `${kmValue} kilometers = ${milesResult} miles`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('kmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('kmValue').focus();
  document.getElementById('kmValue').value = 100;
  updateConversion();
});
</script>

