<script>
function updateConversion() {
  const gajValue = parseFloat(document.getElementById('gajValue').value);
  const sqftResult = document.getElementById('sqftResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(gajValue) || gajValue < 0) {
    sqftResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert gaj to square feet (1 gaj = 9 sqft)
  const sqftValue = gajValue * 9;
  
  // Format the result
  let formattedResult;
  if (sqftValue >= 100000) {
    formattedResult = sqftValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqftValue >= 1000) {
    formattedResult = sqftValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else {
    formattedResult = sqftValue.toFixed(0);
  }
  
  sqftResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const gajValue = document.getElementById('gajValue').value;
  const sqftResult = document.getElementById('sqftResult').value;
  const copyText = `${gajValue} gaj = ${sqftResult} square feet`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('gajValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('gajValue').focus();
  document.getElementById('gajValue').value = 100;
  updateConversion();
});
</script>

