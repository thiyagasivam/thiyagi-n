<script>
let isUpdating = false;

function convertTonRefrigerationToKilowatt(tonRefrigeration) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(tonRefrigeration) && tonRefrigeration !== '') {
        // 1 TR = 3.5169 kW (12,000 BTU/h ÷ 3,412.14 BTU/kWh)
        const kilowatt = tonRefrigeration * 3.5169;
        document.getElementById('kilowattInput').value = kilowatt.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-cyan-600 mr-2"></i>
            <strong>${tonRefrigeration} TR = ${kilowatt.toFixed(3)} kW</strong>
        `;
    } else {
        document.getElementById('kilowattInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertKilowattToTonRefrigeration(kilowatt) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(kilowatt) && kilowatt !== '') {
        // 1 kW = 1/3.5169 TR
        const tonRefrigeration = kilowatt / 3.5169;
        document.getElementById('tonRefrigerationInput').value = tonRefrigeration.toFixed(6);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-cyan-600 mr-2"></i>
            <strong>${kilowatt} kW = ${tonRefrigeration.toFixed(3)} TR</strong>
        `;
    } else {
        document.getElementById('tonRefrigerationInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('tonRefrigerationInput').value = '';
    document.getElementById('kilowattInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const tonRefrigerationValue = document.getElementById('tonRefrigerationInput').value;
    const kilowattValue = document.getElementById('kilowattInput').value;
    
    document.getElementById('tonRefrigerationInput').value = kilowattValue;
    document.getElementById('kilowattInput').value = tonRefrigerationValue;
    
    if (kilowattValue) {
        convertTonRefrigerationToKilowatt(parseFloat(kilowattValue));
    } else if (tonRefrigerationValue) {
        convertKilowattToTonRefrigeration(parseFloat(tonRefrigerationValue));
    }
}

// Add event listeners
document.getElementById('tonRefrigerationInput').addEventListener('input', function() {
    convertTonRefrigerationToKilowatt(parseFloat(this.value));
});

document.getElementById('kilowattInput').addEventListener('input', function() {
    convertKilowattToTonRefrigeration(parseFloat(this.value));
});
</script>

