<script>
function updateConversion() {
  const sqMileValue = parseFloat(document.getElementById('sqMileValue').value);
  const acreResult = document.getElementById('acreResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(sqMileValue) || sqMileValue < 0) {
    acreResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert square miles to acres (1 square mile = 640 acres)
  const acreValue = sqMileValue * 640;
  
  // Format the result with appropriate decimal places and thousands separators
  let formattedResult;
  if (acreValue >= 10000) {
    formattedResult = acreValue.toLocaleString('en-US', {maximumFractionDigits: 0});
  } else if (acreValue >= 1000) {
    formattedResult = acreValue.toLocaleString('en-US', {maximumFractionDigits: 1});
  } else {
    formattedResult = acreValue.toLocaleString('en-US', {maximumFractionDigits: 2});
  }
  
  acreResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const sqMileValue = document.getElementById('sqMileValue').value;
  const acreResult = document.getElementById('acreResult').value;
  const copyText = `${sqMileValue} square miles = ${acreResult} acres`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('sqMileValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('sqMileValue').focus();
  document.getElementById('sqMileValue').value = 1;
  updateConversion();
});
</script>

