<script>
let currentUnit = 'us'; // 'us' or 'uk'

function updateConversion() {
  const mlValue = parseFloat(document.getElementById('mlValue').value);
  const ounceResult = document.getElementById('ounceResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mlValue) || mlValue < 0) {
    ounceResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert milliliters to fluid ounces based on current unit type
  let ounceValue;
  if (currentUnit === 'us') {
    // 1 milliliter = 0.033814 US fluid ounces
    ounceValue = mlValue * 0.033814;
  } else {
    // 1 milliliter = 0.035195 UK fluid ounces
    ounceValue = mlValue * 0.035195;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (ounceValue >= 100) {
    formattedResult = ounceValue.toFixed(1);
  } else if (ounceValue >= 10) {
    formattedResult = ounceValue.toFixed(2);
  } else {
    formattedResult = ounceValue.toFixed(3);
  }
  
  ounceResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mlValue = document.getElementById('mlValue').value;
  const ounceResult = document.getElementById('ounceResult').value;
  const unitType = currentUnit === 'us' ? 'US fluid ounces' : 'UK fluid ounces';
  const copyText = `${mlValue} milliliters = ${ounceResult} ${unitType}`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usOunceBtn');
  const ukBtn = document.getElementById('ukOunceBtn');
  const ounceLabel = document.getElementById('ounceLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    ounceLabel.textContent = 'US Fluid Ounces';
  } else {
    ukBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    ounceLabel.textContent = 'UK Fluid Ounces';
  }
  
  updateConversion();
}

document.getElementById('mlValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usOunceBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('ukOunceBtn').addEventListener('click', () => switchUnit('uk'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mlValue').focus();
  document.getElementById('mlValue').value = 250;
  updateConversion();
});
</script>

