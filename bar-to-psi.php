function updateConversion() {
  const barValue = parseFloat(document.getElementById('barValue').value);
  const psiResult = document.getElementById('psiResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(barValue) || barValue < 0) {
    psiResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert bar to PSI (1 bar = 14.50377377 PSI)
  const psiValue = barValue * 14.50377377;
  
  // Format the result
  let formattedResult;
  if (psiValue >= 1000) {
    formattedResult = psiValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (psiValue >= 1) {
    formattedResult = psiValue.toFixed(2);
  } else {
    formattedResult = psiValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  psiResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const barValue = document.getElementById('barValue').value;
  const psiResult = document.getElementById('psiResult').value;
  const copyText = `${barValue} bar = ${psiResult} PSI`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('barValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('barValue').focus();
  document.getElementById('barValue').value = 2;
  updateConversion();
});
</script>

