<script>
function updateConversion() {
  const mmValue = parseFloat(document.getElementById('mmValue').value);
  const inchesResult = document.getElementById('inchesResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(mmValue) || mmValue < 0) {
    inchesResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert millimeters to inches (1 inch = 25.4 millimeters)
  const inchesValue = mmValue / 25.4;
  
  // Format the result
  let formattedResult;
  if (inchesValue >= 10) {
    formattedResult = inchesValue.toFixed(4);
  } else if (inchesValue >= 1) {
    formattedResult = inchesValue.toFixed(5);
  } else {
    formattedResult = inchesValue.toFixed(6);
  }
  
  inchesResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const mmValue = document.getElementById('mmValue').value;
  const inchesResult = document.getElementById('inchesResult').value;
  const copyText = `${mmValue} millimeters = ${inchesResult} inches`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('mmValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('mmValue').focus();
  document.getElementById('mmValue').value = 25.4;
  updateConversion();
});
</script>

