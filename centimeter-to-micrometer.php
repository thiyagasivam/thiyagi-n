function updateConversion() {
  const cmValue = parseFloat(document.getElementById('cmValue').value);
  const micrometerResult = document.getElementById('micrometerResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(cmValue) || cmValue < 0) {
    micrometerResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert centimeters to micrometers: 1 cm = 10,000 Œºm
  const micrometerValue = cmValue * 10000;
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (micrometerValue >= 10000) {
    formattedResult = micrometerValue.toFixed(0);
  } else if (micrometerValue >= 100) {
    formattedResult = micrometerValue.toFixed(1);
  } else if (micrometerValue >= 1) {
    formattedResult = micrometerValue.toFixed(2);
  } else {
    formattedResult = micrometerValue.toFixed(4);
  }
  
  micrometerResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const cmValue = document.getElementById('cmValue').value;
  const micrometerResult = document.getElementById('micrometerResult').value;
  const copyText = `${cmValue} centimeters = ${micrometerResult} micrometers`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

