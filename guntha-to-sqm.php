<script>
function updateConversion() {
  const gunthaValue = parseFloat(document.getElementById('gunthaValue').value);
  const sqmResult = document.getElementById('sqmResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(gunthaValue) || gunthaValue < 0) {
    sqmResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert guntha to square meters (1 guntha = 101.17 square meters)
  const sqmValue = gunthaValue * 101.17;
  
  // Format the result
  let formattedResult;
  if (sqmValue >= 10000) {
    formattedResult = sqmValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 0 
    });
  } else if (sqmValue >= 100) {
    formattedResult = sqmValue.toFixed(1);
  } else {
    formattedResult = sqmValue.toFixed(2);
  }
  
  sqmResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const gunthaValue = document.getElementById('gunthaValue').value;
  const sqmResult = document.getElementById('sqmResult').value;
  const copyText = `${gunthaValue} guntha = ${sqmResult} square meters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

document.getElementById('gunthaValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('gunthaValue').focus();
  document.getElementById('gunthaValue').value = 10;
  updateConversion();
});
</script>

