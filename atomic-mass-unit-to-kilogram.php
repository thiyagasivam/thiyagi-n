        `;
    } else {
        document.getElementById('kilogramInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertKilogramToAtomicMass(kilogram) {
    if (isUpdating) return;
    isUpdating = true;
    
