<script>
let isUpdating = false;

function formatScientific(value) {
    if (value >= 1e6 || value <= 1e-6) {
        return value.toExponential(3);
    }
    return value.toString();
}

function convertAtomicMassToKilogram(atomicMass) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(atomicMass) && atomicMass !== '') {
        // 1 u = 1.66053906660×10^-27 kg
        const kilogram = atomicMass * 1.66053906660e-27;
        document.getElementById('kilogramInput').value = kilogram;
        
        const formattedKilogram = formatScientific(kilogram);
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-violet-600 mr-2"></i>
            <strong>${atomicMass} u = ${formattedKilogram} kg</strong>
        `;
    } else {
        document.getElementById('kilogramInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertKilogramToAtomicMass(kilogram) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(kilogram) && kilogram !== '') {
        // 1 kg = 6.02214076×10^26 u
        const atomicMass = kilogram / 1.66053906660e-27;
        document.getElementById('atomicMassInput').value = atomicMass;
        
        const formattedKilogram = formatScientific(kilogram);
        const formattedAtomicMass = formatScientific(atomicMass);
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-violet-600 mr-2"></i>
            <strong>${formattedKilogram} kg = ${formattedAtomicMass} u</strong>
        `;
    } else {
        document.getElementById('atomicMassInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('atomicMassInput').value = '';
    document.getElementById('kilogramInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const atomicMassValue = document.getElementById('atomicMassInput').value;
    const kilogramValue = document.getElementById('kilogramInput').value;
    
    document.getElementById('atomicMassInput').value = kilogramValue;
    document.getElementById('kilogramInput').value = atomicMassValue;
    
    if (kilogramValue) {
        convertAtomicMassToKilogram(parseFloat(kilogramValue));
    } else if (atomicMassValue) {
        convertKilogramToAtomicMass(parseFloat(atomicMassValue));
    }
}

// Add event listeners
document.getElementById('atomicMassInput').addEventListener('input', function() {
    convertAtomicMassToKilogram(parseFloat(this.value));
});

document.getElementById('kilogramInput').addEventListener('input', function() {
    convertKilogramToAtomicMass(parseFloat(this.value));
});
</script>

