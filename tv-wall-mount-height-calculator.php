                const lighting = document.getElementById('lighting').value;
                const tiltAngle = parseFloat(document.getElementById('tiltAngle').value);
                const viewingPreference = document.getElementById('viewingPreference').value;

                if (!tvSize || !viewingDistance || !seatingHeight) {
                    alert('Please fill in all required fields.');
                    return;
                }

                // Calculate TV dimensions
                const tvHeight = this.getTVHeight(tvSize);
                const tvWidth = this.getTVWidth(tvSize);

                // Calculate optimal viewing height
                const eyeLevelFromFloor = seatingHeight + eyeLevel;
                
                // Base optimal height calculation
                let optimalHeight = eyeLevelFromFloor;

                // Adjust for viewing preference
                const preferenceAdjustments = {
                    'standard': 0,
                    'cinema': 3,
                    'comfort': -2,
                    'space-saving': 6
                };
                optimalHeight += preferenceAdjustments[viewingPreference] || 0;

                // Room type adjustments
                const roomAdjustments = {
                    'living-room': 0,
                    'bedroom': -2,
                    'basement': -1,
                    'media-room': 1,
                    'office': 2,
                    'kitchen': 4
                };
                optimalHeight += roomAdjustments[roomType] || 0;

                // Lighting adjustments (higher for bright rooms to reduce glare)
                const lightingAdjustments = {
                    'bright': 2,
                    'moderate': 0,
                    'dim': -1,
                    'controlled': 0
                };
                optimalHeight += lightingAdjustments[lighting] || 0;

                // Distance-based adjustments (THX guidelines)
                const optimalDistance = tvSize * 1.5; // Optimal distance in inches
                const actualDistance = viewingDistance * 12; // Convert feet to inches
                
                if (actualDistance > optimalDistance * 1.2) {
                    optimalHeight += 2; // Slightly higher for far viewing
                } else if (actualDistance < optimalDistance * 0.8) {
                    optimalHeight -= 1; // Slightly lower for close viewing
                }

                // Calculate viewing angle
                const heightDifference = optimalHeight - eyeLevelFromFloor;
                const verticalAngle = Math.atan(heightDifference / actualDistance) * (180 / Math.PI);

                // Calculate recommended tilt
                let recommendedTilt = 0;
                if (verticalAngle > 5) {
                    recommendedTilt = Math.min(verticalAngle - 2, tiltAngle);
                }

                // Calculate height range (Â±4 inches is acceptable)
                const minHeight = Math.max(optimalHeight - 4, eyeLevelFromFloor - 6);
                const maxHeight = Math.min(optimalHeight + 4, ceilingHeight * 12 - tvHeight / 2 - 6);

                // Calculate mount center height (TV center minus half TV height)
                const mountHeight = optimalHeight - tvHeight / 2;

                // THX recommendation (center of screen at eye level)
                const thxHeight = eyeLevelFromFloor;

                this.results = {
                    tvSize,
                    tvHeight,
                    tvWidth,
                    viewingDistance,
                    optimalHeight,
                    minHeight,
                    maxHeight,
                    mountHeight,
                    verticalAngle,
                    recommendedTilt,
                    eyeLevelFromFloor,
                    thxHeight,
                    roomType,
                    mountType,
                    lighting,
                    ceilingHeight,
                    seatingHeight,
                    viewingPreference
                };

                this.displayResults();
            }

