                // 16:9 aspect ratio calculation
                return (diagonal * 9) / Math.sqrt(16*16 + 9*9);
            }

            getTVWidth(diagonal) {
                // 16:9 aspect ratio calculation
                return (diagonal * 16) / Math.sqrt(16*16 + 9*9);
            }

