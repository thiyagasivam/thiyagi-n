<script>
let isUpdating = false;

function convertLbfFtToNewtonMeter(lbfFt) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(lbfFt) && lbfFt !== '') {
        // 1 lbf·ft = 1.3558179483314004 N·m
        const newtonMeter = lbfFt * 1.3558179483314004;
        document.getElementById('newtonMeterInput').value = newtonMeter.toFixed(4);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-red-600 mr-2"></i>
            <strong>${lbfFt} lbf·ft = ${newtonMeter.toFixed(2)} N·m</strong>
        `;
    } else {
        document.getElementById('newtonMeterInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function convertNewtonMeterToLbfFt(newtonMeter) {
    if (isUpdating) return;
    isUpdating = true;
    
    if (!isNaN(newtonMeter) && newtonMeter !== '') {
        // 1 N·m = 1/1.3558179483314004 lbf·ft
        const lbfFt = newtonMeter / 1.3558179483314004;
        document.getElementById('lbfFtInput').value = lbfFt.toFixed(4);
        
        document.getElementById('result').innerHTML = `
            <i class="fas fa-equals text-red-600 mr-2"></i>
            <strong>${newtonMeter} N·m = ${lbfFt.toFixed(2)} lbf·ft</strong>
        `;
    } else {
        document.getElementById('lbfFtInput').value = '';
        document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
    }
    
    isUpdating = false;
}

function clearValues() {
    document.getElementById('lbfFtInput').value = '';
    document.getElementById('newtonMeterInput').value = '';
    document.getElementById('result').innerHTML = 'Enter a value to see the conversion result';
}

function swapValues() {
    const lbfFtValue = document.getElementById('lbfFtInput').value;
    const newtonMeterValue = document.getElementById('newtonMeterInput').value;
    
    document.getElementById('lbfFtInput').value = newtonMeterValue;
    document.getElementById('newtonMeterInput').value = lbfFtValue;
    
    if (newtonMeterValue) {
        convertLbfFtToNewtonMeter(parseFloat(newtonMeterValue));
    } else if (lbfFtValue) {
        convertNewtonMeterToLbfFt(parseFloat(lbfFtValue));
    }
}

// Add event listeners
document.getElementById('lbfFtInput').addEventListener('input', function() {
    convertLbfFtToNewtonMeter(parseFloat(this.value));
});

document.getElementById('newtonMeterInput').addEventListener('input', function() {
    convertNewtonMeterToLbfFt(parseFloat(this.value));
});
</script>

