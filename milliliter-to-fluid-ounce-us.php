<script>
function updateConversion() {
  const mlInput = document.getElementById('mlValue');
  const mlValue = parseFloat(mlInput.value);
  const flozResult = document.getElementById('flozResult');
  const copyBtn = document.getElementById('copyBtn');
  const liveConversion = document.getElementById('liveConversion');
  const conversionDisplay = document.getElementById('conversionDisplay');
  
  if (isNaN(mlValue) || mlValue < 0) {
    flozResult.value = '';
    copyBtn.classList.add('hidden');
    liveConversion.classList.add('hidden');
    flozResult.classList.remove('result-highlight');
    return;
  }
  
  // Convert milliliters to US fluid ounces (1 ml = 0.033814 fl oz)
  const flozValue = mlValue * 0.033814;
  
  let formattedResult = flozValue.toLocaleString('en-US', { 
    minimumFractionDigits: 0, 
    maximumFractionDigits: 6 
  });
  
  flozResult.value = formattedResult;
  flozResult.classList.add('result-highlight');
  copyBtn.classList.remove('hidden');
  
  const formattedML = mlValue.toLocaleString('en-US');
  conversionDisplay.innerHTML = `${formattedML} ml = ${formattedResult} fl oz`;
  liveConversion.classList.remove('hidden');
  
  mlInput.classList.add('border-blue-500');
}

function copyResult() {
  const mlValue = document.getElementById('mlValue').value;
  const flozResult = document.getElementById('flozResult').value;
  const copyText = `${mlValue} ml = ${flozResult} fl oz`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    showCopySuccess();
  }).catch(() => {
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    showCopySuccess();
  });
}

function showCopySuccess() {
  const copySuccess = document.getElementById('copySuccess');
  const copyBtn = document.getElementById('copyBtn');
  
  copySuccess.classList.remove('hidden');
  copyBtn.innerHTML = '<i class="fas fa-check"></i> Copied!';
  copyBtn.classList.add('bg-green-600', 'hover:bg-green-700');
  
  setTimeout(() => {
    copySuccess.classList.add('hidden');
    copyBtn.innerHTML = '<i class="fas fa-copy"></i> Copy Result';
    copyBtn.classList.remove('bg-green-600', 'hover:bg-green-700');
  }, 3000);
}

function clearAll() {
  document.getElementById('mlValue').value = '';
  document.getElementById('flozResult').value = '';
  document.getElementById('copyBtn').classList.add('hidden');
  document.getElementById('liveConversion').classList.add('hidden');
  document.getElementById('flozResult').classList.remove('result-highlight');
  document.getElementById('mlValue').classList.remove('border-blue-500');
  document.getElementById('mlValue').focus();
}

document.getElementById('mlValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('clearBtn').addEventListener('click', clearAll);

window.addEventListener('DOMContentLoaded', () => {
  const mlInput = document.getElementById('mlValue');
  mlInput.focus();
  
  setTimeout(() => {
    mlInput.value = 250;
    updateConversion();
  }, 500);
});
</script>

