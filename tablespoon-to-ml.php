<script>
let currentUnit = 'us'; // 'us' or 'metric'

function updateConversion() {
  const tablespoonValue = parseFloat(document.getElementById('tablespoonValue').value);
  const mlResult = document.getElementById('mlResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(tablespoonValue) || tablespoonValue < 0) {
    mlResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert tablespoons to milliliters based on current unit type
  let mlValue;
  if (currentUnit === 'us') {
    // 1 US tablespoon = 14.787 milliliters
    mlValue = tablespoonValue * 14.787;
  } else {
    // 1 metric tablespoon = 15 milliliters
    mlValue = tablespoonValue * 15;
  }
  
  // Format the result with appropriate decimal places
  let formattedResult;
  if (mlValue >= 100) {
    formattedResult = mlValue.toFixed(1);
  } else if (mlValue >= 10) {
    formattedResult = mlValue.toFixed(2);
  } else {
    formattedResult = mlValue.toFixed(3);
  }
  
  mlResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const tablespoonValue = document.getElementById('tablespoonValue').value;
  const mlResult = document.getElementById('mlResult').value;
  const unitType = currentUnit === 'us' ? 'US tablespoons' : 'metric tablespoons';
  const copyText = `${tablespoonValue} ${unitType} = ${mlResult} milliliters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

function switchUnit(unit) {
  currentUnit = unit;
  const usBtn = document.getElementById('usTablespoonBtn');
  const metricBtn = document.getElementById('metricTablespoonBtn');
  const tablespoonLabel = document.getElementById('tablespoonLabel');
  
  if (unit === 'us') {
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    tablespoonLabel.textContent = 'US Tablespoons';
  } else {
    metricBtn.className = 'px-3 py-2 rounded-md text-sm font-medium bg-indigo-600 text-white';
    usBtn.className = 'px-3 py-2 rounded-md text-sm font-medium text-gray-700 hover:bg-gray-200';
    tablespoonLabel.textContent = 'Metric Tablespoons';
  }
  
  updateConversion();
}

document.getElementById('tablespoonValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);
document.getElementById('usTablespoonBtn').addEventListener('click', () => switchUnit('us'));
document.getElementById('metricTablespoonBtn').addEventListener('click', () => switchUnit('metric'));

// Autofocus and demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('tablespoonValue').focus();
  document.getElementById('tablespoonValue').value = 3;
  updateConversion();
});
</script>

