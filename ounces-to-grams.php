function updateConversion() {
  const ouncesValue = parseFloat(document.getElementById('ouncesValue').value);
  const gramsResult = document.getElementById('gramsResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(ouncesValue) || ouncesValue < 0) {
    gramsResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert ounces to grams (1 ounce = 28.349523125 grams)
  const gramsValue = ouncesValue * 28.349523125;
  
  // Format the result
  let formattedResult;
  if (gramsValue >= 1000) {
    formattedResult = gramsValue.toLocaleString('en-US', { 
      minimumFractionDigits: 0, 
      maximumFractionDigits: 1 
    });
  } else if (gramsValue >= 1) {
    formattedResult = gramsValue.toFixed(2);
  } else {
    formattedResult = gramsValue.toFixed(4);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  gramsResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const ouncesValue = document.getElementById('ouncesValue').value;
  const gramsResult = document.getElementById('gramsResult').value;
  const copyText = `${ouncesValue} ounces = ${gramsResult} grams`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('ouncesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('ouncesValue').focus();
  document.getElementById('ouncesValue').value = 4;
  updateConversion();
});
</script>

