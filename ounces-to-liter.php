function updateConversion() {
  const ouncesValue = parseFloat(document.getElementById('ouncesValue').value);
  const literResult = document.getElementById('literResult');
  const copyBtn = document.getElementById('copyBtn');
  
  if (isNaN(ouncesValue) || ouncesValue < 0) {
    literResult.value = '';
    copyBtn.classList.add('hidden');
    return;
  }
  
  // Convert fluid ounces to liters (1 fl oz = 0.0295735296875 L)
  const literValue = ouncesValue * 0.0295735296875;
  
  // Format the result
  let formattedResult;
  if (literValue >= 1) {
    formattedResult = literValue.toFixed(3);
  } else if (literValue >= 0.1) {
    formattedResult = literValue.toFixed(4);
  } else {
    formattedResult = literValue.toFixed(6);
  }
  
  // Remove trailing zeros
  formattedResult = parseFloat(formattedResult).toString();
  
  literResult.value = formattedResult;
  copyBtn.classList.remove('hidden');
}

function copyResult() {
  const ouncesValue = document.getElementById('ouncesValue').value;
  const literResult = document.getElementById('literResult').value;
  const copyText = `${ouncesValue} fluid ounces = ${literResult} liters`;
  
  navigator.clipboard.writeText(copyText).then(() => {
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  }).catch(err => {
    // Fallback for older browsers
    const textArea = document.createElement('textarea');
    textArea.value = copyText;
    document.body.appendChild(textArea);
    textArea.select();
    document.execCommand('copy');
    document.body.removeChild(textArea);
    
    const copySuccess = document.getElementById('copySuccess');
    copySuccess.classList.remove('hidden');
    setTimeout(() => {
      copySuccess.classList.add('hidden');
    }, 3000);
  });
}

// Event listeners
document.getElementById('ouncesValue').addEventListener('input', updateConversion);
document.getElementById('copyBtn').addEventListener('click', copyResult);

// Initialize with demo value
window.addEventListener('DOMContentLoaded', () => {
  document.getElementById('ouncesValue').focus();
  document.getElementById('ouncesValue').value = 16;
  updateConversion();
});
</script>

